                    <p class="mbr-text mbr-fonts-style display-7">Hijronda oʻrtangan oshiq mahbubaga oʻtinadi. U raqibi
                        oldida gʻamdan qiynalgan holatini namoyon qilishni istamaydi. Shoir oshiqning gʻamgin holatini,
                        tunu kun iztirob chekayotganini ifodalash maqsadida tazod san`atidan foydalanadi.
                        <br>
                        <br>Ma`lumki, mumtoz adabiyotda tazod san`ati ikki usulda qoʻlaniladi. Birinchi usulda tazod
                        gʻazalning ayrim baytlaridagina keltirilsa, ikkinchidan, gʻazal boshdan-oxirigacha tazod san`ati
                        asosiga quriladi. Jumladan, Murodiy “koʻrub” radifli gʻazalida tazodning birinchi usuliga
                        murojaat qiladi va ikki baytda bu san`atdan unumli foydalanadi. Dastlabki baytda bevosita oʻz
                        fikrini davom qildirib, gʻamgin koʻngil oy yuzli parini koʻrgach, dard bu xursandchilikdan
                        uyaladi. Bir baytning oʻzida shoir tazod va tashbeh san`atlarini uygʻunlikda qoʻllaydi:
                        <br>
                        <br>Mahzun koʻngul shod oʻlgʻusi ul mohitobonin koʻrub,
                        <br>
                        <br>Goʻyoki dard ahli boʻlur masrur darmonin koʻrub (B-6).
                        <br>
                        <br>Keyingi baytda shoir tazod san`atini bevosita harakat va holat zidligi asosida yaratadi.
                        Ma`shuqaning zulfi parishonligiga monand ravishda oshiqning koʻngli ham xotirjam emas:
                        <br>
                        <br>Man zorgʻa har tun agar boʻlsa muyassar orazi,
                        <br>
                        <br>Jam` aylay olmon koʻnglumi zulfi parishonin koʻrub.
                        <br>
                        <br>Shoir ma`shuqaning goʻzalligini ifodalash uchun an`anaviy “tovus” obrazidan foydalandi.
                        Ma`lumki, bu obraz mumtoz adabiyotimizda yorning yurishiga qiyos qilinadi. Biroq Murodiy
                        gʻazalidagi tovus mahbubaning raftorini koʻrgach, xijolatdan yuziga chodir tortadi. Shuningdek,
                        yorni koʻrgach, gʻuncha tabassum qilishdan bosh tortganidek, nargis mahbubaning koʻzlaridan
                        uyaladi. Yorning koʻzlari fitnakor, ular oshiqning koʻnglini notinch qilishga ham erishadi.
                        Shoir buni tashbeh, istiora va mubolagʻa san`atlari orqali tasvirlaydi:
                        <br>
                        <br>Gʻuncha tabassumdin qolur gar koʻrsa sayri bogʻ aro,
                        <br>
                        <br>Nargisga xijlatdur base ul chashmi fattonin koʻrub
                        <br>
                        <br>Tovus tortar boshigʻa sharmi xijolat chodirin,
                        <br>
                        <br>Gar jilva qilsa ul pari sarvi xiromonin koʻrub (B-6).
                        <br>
                        <br>Shoirning ta`biricha, ma`shuqaning yuzu koʻzi nuridan tunu kun nurafshon, biroq bu nurning
                        parvonasi kimligi shoirga noma`lum. Toʻgʻrirogʻi, u baytda tajohuli orif san`atini qoʻllab,
                        bilib bilmaslikka oladi. Baytning badiiyligini oshirish maqsadida tazodning goʻzal namunasini
                        yaratadi:
                        <br>
                        <br>Ravshan edikim, yuz koʻzi birla tun-kun,
                        <br>
                        <br>Oyo, bu kecha sham`igʻa parvona kim erkan?
                        <br>
                        <br>Ya`qubsifat kecha-kunduz chekarman oh,
                        <br>
                        <br>Bildirguchi holim mahi Kan`ona kim erkan?
                        <br>
                        <br>Ma`lumki, parvona – oshiq, u yorning atrofida girdikapalak boʻlsa-da, vasliga yetolmaydi. Bu
                        baytlarda shoir tashbeh, tazod, talmeh san`atlaridan mohirona foydalanadi. Ya`qub va Yusufi
                        Kan`on obrazlariga murojaat qilib, oshiqning iztirobiyu, ma`shuqaning husnu latofatini kengroq
                        ifodalaydi. Ya`ni oshiq Ya`qubdek hijronda oʻrtanadi. Ma`lumki, iztirob, gʻam-alam, farzand
                        dogʻi Ya`qubning koʻzlarini ojiz qiladi. Yusufi Kan`on goʻzallikda tengi yoʻq paygʻambar,
                        goʻzallik timsolidir. Oshiq ham mahbuba koʻyida oʻrtanadi, oh chekadi. Biroq yor bundan bexabar,
                        unga kimdir ma`lum qilishi lozim.
                        <br>
                        <br>Murodiyning Feruzga bagʻishlangan gʻazallarida talmehning goʻzal namunalarini koʻramiz.
                        Talmeh orqali shoir oʻz fikrini moʻ`jaz ifodalashga erishadi. Ya`ni “bu shoirga bir ishora bilan
                        chuqur ma`no ifodalash imkonini beruvchi san`atdir”[1]. Murodiy jahonga mashhur hukmdorlarning
                        nomini keltirish bilan Feruzga boʻlgan hurmatini ifodalaydi:
                        <br>
                        <br>Meni dagʻi yuz ming nasr ojiz ila arz aylayin,
                        <br>
                        <br>Ey Iskandar, Jamshid, Xisravi Doro agʻniyo (B-15).
                        <br>
                        <br>Garchi bu oʻrinda shoir mubolagʻa san`atiga murojaat qilgan boʻlsa-da, Feruz shahzodalarga
                        ham homiy, ham murabbiy sifatida qadrli ekanligi bizga ma`lum.
                        <br>
                        <br>Murodiy baytlarining badiiy yetukligi uning ijodiy salohiyatidan, iqtidoridan dalolatdir.
                        <br>
                        <br>“Haft shuaro” majmuasidagi gʻazallar orasida Sultoniy gʻazaliyoti badiiyati va mazmuni bilan
                        oʻziga xoslikka ega. Shoirning Feruzga payrav tarzida bitilgan “mening” radifli gʻazalining
                        dastlabki baytida tansiq-us-sifot san`ati mohirona qoʻllanadi. Bu maqsadning toʻliqroq roʻyobga
                        chiqishiga xizmat qilgan. Yorning gul sayriga kelmasligi besabab emas. Uning har bir sifati
                        gullarni uyaltirishga qodir:
                        <br>
                        <br>Kelmadi gul sayriga shoʻxi parizodim mening,
                        <br>
                        <br>Sochi sunbul, yuzi gul, ul qaddi shamshodim mening (B-48).
                        <br>
                        <br>Shoir gʻazalda boshdan-oxirigacha mubolagʻa san`atini qoʻllab, oshiqning iztiroblarini
                        toʻliq ifodalashga harakat qiladi. Ma`shuqaning sochi sunbulga, yuzi gulga, qaddi esa shamshod
                        daraxtiga qiyos qilinadi, ya`ni Sultoniy mumtoz adabiyotdagi tayyor tashbehlardan ijodiy
                        foydalanadi. Mubolagʻali tarzda oshiqning ohlari falakkacha yetadi:
                        <br>
                        <br>Mehri ruxsori firoqida chekarman oʻtligʻ oh,
                        <br>
                        <br>Yetgusi soʻyi samoga ohu faryodim mening.
                        <br>
                        <br>Ma`lumki, mehr – quyosh, issiqlik va yorugʻlik manbai ma`nosida qoʻllaniladi. Baytning
                        dastlabki misrasida shoir istioraga murojaat qilib, yorning ruxsorini mehrga qiyos qiladi.
                        Gʻazal maqtasida esa talmeh san`atini qoʻllab, Feruz misralaridan deyarli koʻchiradi. Chunki
                        Feruz ham “aylading” radifli gʻazalni Ogahiyga payrav tarzida bitarkan, maqtani shunday
                        yaratadi:
                        <br>
                        <br>Komil ersam nazm aro Feruzdek, ermas ajab,
                        <br>
                        <br>Ogahiydur she`r mashq aylarda ustodim mening[2].
                        <br>
                        <br>Sultoniyda esa misralar quyidagicha hamohang tarzda bitiladi:
                        <br>
                        <br>Tong emas, she`ringga tahsin qilsa xalq, Sultoniyo,
                        <br>
                        <br>Mashqi she`r aylardadur Feruz ustodim mening.
                        <br>
                        <br>Sultoniy – Ogahiy poetik mahoratidan bahramand boʻlgan shoir. Quyidagi baytlarda u Ogahiy
                        fikrini davom qildirib, tashbeh, mubolagʻa, talmeh san`atlarini uygʻunlikda qoʻllaydi:
                        <br>
                        <br>Oydek yuzing anvoridin ravshan erur jumla jahon,
                        <br>
                        <br>Bu husningu diydoringga olam gado, men ham gado (B-37).
                        <br>
                        <br>Keyingi baytda oʻz nafasi bilan oʻlikka jon bagʻishlagan Iso haqidagi an`anaviy qarashni
                        takrorlaydi. Gʻazal radifi ham oʻziga xos boʻlib, besh soʻzdan tarkib topib, tayanch soʻz
                        “gado”dir.
                        <br>
                        <br>Misralarda fikr ixcham va ravon ifodalangan boʻlib, badiiy vositalarga boy. “O`lgan badan”,
                        “shirin soʻz”, “Isosifat guftor”, “ravshan erur”, kabi sifatlashlar gʻazalning ta`sirchanligini
                        oshiradi. Gʻazalda “raftor, ruxsor, gulzor, diydor, guftor” kabi soʻzlarning ohangdosh boʻlib
                        kelishi an`anaviylikka amal qilishdan dalolatdir. Goʻzallikda beqiyos mahbuba latofatiga olam
                        gado boʻlgach, oshiq gado boʻlmasinmi? Uning birgina shirin soʻzi oʻlikka jon bagʻishlaydi.
                        <br>
                        <br>Sultoniy har bir gʻazalida yangicha ifoda yaratishga, mazmundor fikrlar aytishga harakat
                        qilgan. Bayoniy ta`biri bilan aytganda:
                        <br>
                        <br>Oningtek she`r uslubida mohir,
                        <br>
                        <br>Mohir boʻlubdurlar bu sirru zohir.
                        <br>
                        <br>Darhaqiqat, badiiy vositalarga boy misralar buni bevosita isbotlaydi. Shoirning asosiy
                        qahramoni oshiqdir. U “mahliqo jonon”ni koʻrganda, lutfiga umid bogʻlaganidek, mahbuba uni
                        koʻrganda sitamlarga mubtalo qilishga odatlangan:
                        <br>
                        <br>Qolgʻaymu sabru toqat ila jahonda ahbob,
                        <br>
                        <br>Ul shoʻx mahliqodin nozu adoni koʻrgach (B-46).
                        <br>
                        <br>Baytdagi “shoʻx mahliqo” sifatlashi orqali ta`sirchanlik ifodalanadi. Gʻazalning keyingi
                        baytida shoir talmeh va tazod san`atlarini qoʻllaydi. Oshiq yor ishqida “Majnunshior” boʻlgani
                        bois “kecha-kunduz” alamlarga giriftor:
                        <br>
                        <br>Majnunshior boʻlubdur ishqida kecha-kunduz,
                        <br>
                        <br>Sultoniy ul parivash qoshi qaroni koʻrgach (B-46).
                        <br>
                        <br>Shoirning har bir gʻazalida ishqning yangi qirralari kashf etiladi. “Kuydi jonim ul pari
                        oraz namoyon aylagach” deb boshlanuvchi gʻazalida Sultoniy ma`shuqa goʻzalligini ta`rif etadi.
                        Mazkur gʻazalda shoir matladan to maqtagacha badiiy vositalardan unumli foydalanadiki, bu
                        gʻazalning mazmun va ta`sirchanligini oshiradi:
                        <br>
                        <br>Kuydi jonim ul pari oraz namoyon aylagach,
                        <br>
                        <br>Husnidin gulshanni rashki bogʻi rizvon aylagach (B-47).
                        <br>
                        <br>Mahbubaning ruxsorini bir koʻrgan oshiqning joni oʻt boʻlib yonadi. Bu xuddi gulshan uning
                        husnini rashk qilganiga oʻxshaydi. Zero, mahbuba ruxsori oldida gulshan oddiy bir narsadir.
                        Istioraning goʻzal namunasini yaratgan shoir soʻnggi baytlarda fikrini bevosita davom ettiradi.
                        “Sarv xijlat ichra qoldi qaddi zebosin koʻrub”, “Gul niqobin chok etib, bagʻrin toʻla qon
                        ayladi” kabi tashxislarni mubolagʻali tarzda qoʻllab, ixcham fikr aytishga intiladi. Qomatining
                        goʻzalligidan sarv uyalganidek, gul sharmandalikdan niqobini chok qilgani bois bagʻri qonga
                        toʻladi. Bu oʻrinda shoir tashxisga husni ta`lil san`atini uygʻunlashtirib, oʻz fikrini chiroyli
                        dalillashga intiladi. Shuningdek, yorning zulfi gul yuzining ustida parishon aylasa, “Aqlu hushu
                        sabru toqat barchasi firor” qiladi. Demak, oshiqning mahbubasi – goʻzallikda tengi yoʻq malak.
                        Biroq u “Koʻz uchidin bir nigah ul la`li xandon aylagach” Sultoniy (lirik qahramon) oshiqlar
                        qatorida boʻlishi mumkin singari istiora bilan shoir gʻazalni yakunlaydi. Shu birgina gʻazalning
                        oʻzi shoirning iste`dodini koʻrsatadi.
                        <br>
                        <br>Sultoniy gʻazaliyotining aksariyat qismini muhabbatni madh etuvchi she`rlar tashkil etadi.
                        Ularning har birida oshiqning dardlari, ma`shuqaning qiliqlari yangilanadi. Jumladan, quyidagi
                        baytda husni ta`lil san`atiga murojaat qilib, oʻz fikrini chiroyli dalillaydi. Oshiqning yorni
                        koʻrgach, oh chekishi bulbullar xonishiga oʻxshaydi:
                        <br>
                        <br>Navolar tortaram ul gul yuzi to oshkor oʻlgʻach,
                        <br>
                        <br>Hazin bulbullar aylar nola chun fasli bahor oʻlgʻach (B-47).
                        <br>
                        <br>Shoir nafaqat she`riy san`atlarni qoʻllashga, balki badiiy vositalar, qofiya va radiflarga
                        ham alohida e`tibor beradi. “Hazin bulbullar”, “Sabrim uyi vayron boʻlib” kabi sifatlash va
                        istiora, mubolagʻali misralar yoxud “oshkor, bahor, yor, tor-tor, gul`uzor, barqaror, zebonigor,
                        beixtiyor” singari soʻzlarning qofiya sifatida tanlanishi fikrimizni isbotlaydi. Shuningdek,
                        <br>
                        <br>Mani Majnun deb ayb aylama, ey zohidi xudbin,
                        <br>
                        <br>Qolarmu aqlu hushim bir pari ruxsora yor oʻlgʻach,
                        <br>
                        <br>tarzidagi misralarda talmehning ifodalanishi yoki:
                        <br>
                        <br>Ochar majlis aro ruxsori gulzorin base gullar,
                        <br>
                        <br>Icharga la`lgun may mashomidin dil ul zebo nigor oʻlgʻach,
                        <br>
                        <br>kabi baytda tashxis va mubolagʻaning uygʻunlashtirilishi gʻazalning badiiy yetuk va
                        ta`sirchanligini ta`minlaydi. O`z navbatida, bu baytlar orqali kitobxon mahbuba goʻzalligini
                        toʻliqroq tasavvur qila oladi.
                        <br>
                        <br>Sultoniy ham gʻazallarida oʻzbek mumtoz adabiyotida an`anaga aylangan “gul” obraziga
                        murojaat qilib, goʻzallik va nafosat timsoli gulga mahbubani qiyos qiladi. Shoirning “gul”
                        radifli gʻazali bunga misol boʻladi:
                        <br>
                        <br>Boʻldi koʻrgach orazing devona gul,
                        <br>
                        <br>Sham`i ruxsoringga chun parvona gul.
                        <br>
                        <br>Jilva qilsang bogʻ aro, ey guluzor,
                        <br>
                        <br>Sadqa boʻlgʻay boshinga aylona gul (B- 62).
                        <br>
                        <br>Anglashiladiki, gulning oʻzi jozibador boʻlsa-da, u inson goʻzalligiga oshiq. Gʻazal
                        baytlari uchun shoir istiora san`atini tanlagan. Ya`ni, “gul” timsolini shoir omonatga oladi va
                        u orqali yorning goʻzalligini ochib beradi. Gul goʻzallikdan devona boʻlish bilan birga, yorning
                        chehrasiga parvona hamdir. Bu bilan she`rda dunyoning oʻtkinchiligiga ishora qilinadi. Parvona
                        ishq yoʻlida halok boʻlsa, sham` yonib tugaydi. Yorning bir jilvasiga gul boshidan aylanib sadqa
                        boʻlishi tabiiy. “Gul” obraziga murojaat qilish xalq ogʻzaki ijodidan boshlangan boʻlib, yozma
                        adabiyotda ham keng qoʻllaniladi. Navoiy gʻazaliyotida uning ajoyib namunalari mavjud. Sultoniy
                        she`riyatining takomilida Navoiy ijodining ta`siri seziladi. Navoiy ijodidagi:
                        <br>
                        <br>Orazingni bogʻ aro chun koʻrdi, hayron boʻldi gul,
                        <br>
                        <br>Bargsiz qoldi, nedinkim, bas parishon boʻldi gul,[3] -
                        <br>
                        <br>kabi baytlar shoirga ilhom manbai boʻlgani shubhasiz. Shoir Navoiydan ijodiy ta`lim olib,
                        “gul” obrazining yangi qiyofasini ochib beradi. Shu bois u gʻazal maqta`sida yorni guldan ham
                        rashk qiladi. Shoir gʻazalning deyarli barcha baytlarida tashxis va mubolagʻa san`atini
                        qoʻllagan. Maqtada esa tazodga murojaat qiladi:
                        <br>
                        <br>Rashkdin Sultoniy tun-kun oʻrtanur,
                        <br>
                        <br>Ilkiga olsa agar jonona gul.
                        <br>
                        <br>“Gul” radifli gʻazalni mumtoz adabiyotda koʻplab shoirlar, jumladan Ogahiy, Feruz, Avaz
                        ijodida ham uchratamiz.
                        <br>
                        <br>Shoirning “Kelib kulbam aro bir kecha qilgʻil” deb boshlanuvchi gʻazalida esa oshiqning
                        gʻam-anduhlari cheksizligini afsonaviy oshiqlar obrazi orqali tasvirlashga erishgan:
                        <br>
                        <br>Oy yuzidin majlisim bu kecha jannat bogʻidur,
                        <br>
                        <br>Mutribimdur bu kecha bir dilbari Uzro misol (B-64).
                        <br>
                        <br>yoxud keyingi baytning “Kel, ey Laylivashim, lutf aylabon bazmim aro bir kun” misrasidagi
                        afsonaviy ma`shuqa obrazlari talmehning yorqin namunasidir.
                        <br>
                        <br>Sultoniy she`riyatida nafaqat ishq mavzusi, balki islom dini aqidalariga amal qilish,
                        Ollohga itoat gʻoyalari ham ancha salmoqli. Zero, shoir din va uning shartlarini bilgan e`tiqodi
                        mustahkam shaxs boʻlgan. Ijodda muayyan yutuqlarga erishmoq ilinjida Ollohdan madad soʻraydi va
                        tashbeh san`ati orqali oʻzini bulbulga qiyos qiladi hamda takrirdan foydalanadi. Baytda yuklama
                        takriri orqali ma`no boʻrttiriladi:
                        <br>
                        <br>Ajab sone` erursen, zotinga xayli bashar qilsa,
                        <br>
                        <br>Tafakkur har necha, ne ibtido, ne intiho paydo.
                        <br>
                        <br>Sultoniy gʻazaliyotida takrirning birikma tarzidagi takroriga ham namunalarni uchratishimiz
                        mumkin. Ma`lumki, takrir san`ati Yo. Ishoqov ta`kidlaganidek: “Takrirning mazmun jihatdan ikki
                        tipi mavjud: bu leksik takror va frazeologik takrordir. Frazeologik takror ibora va soʻz
                        birikmasi shaklidagi takrorni oʻz ichiga oladi”[4]. Sultoniy ijodida takrirning har ikkala
                        turini ham kuzatish mumkin. Yuqoridagi baytda leksik takror bir misraning oʻziga singdirilgan.
                        “Bir taraf” radifli gʻazalida esa frazeologik takrir she`rning har bir baytida qoʻllangan:
                        <br>
                        <br>Boʻston aro gul bir taraf, bechora bulbul bir taraf,
                        <br>
                        <br>Faryodi gʻulgʻul bir taraf, hadsiz tagʻoful bir taraf (B-277).
                        <br>
                        <br>Aynan shunday radifli gʻazalni Bayoniy va Avaz she`riyatida ham uchratamiz. Sultoniy
                        misralari Avaz gʻazaliga hamohang boʻlgani bois qiyos uchun ustoz shoir gʻazalidan bir bayt
                        keltiramiz. Bu esa Avaz she`riyati ham shahzoda shoirlarga ustozlik vazifasini oʻtaganligini
                        bildiradi:
                        <br>
                        <br>Gulshan aro gul bir taraf, ul yuzi anvar bir taraf,
                        <br>
                        <br>Ishq ichra bulbul bir taraf, man zori muztar bir taraf[5].
                        <br>
                        <br>Majmuadan oʻrin olgan Sodiq she`riyati ham oʻziga xos uslub va mavqega ega. Sodiq adabiy
                        an`analarni davom qildirgani holda badiiy san`at va tasviriy vositalarni oʻz gʻazallarida
                        mohirona qoʻllaydi:
                        <br>
                        <br>Karam aylab birovga sarvi guldek qiymati ruxsor,
                        <br>
                        <br>Birovga ayladi qumriyu bulbuldek figʻon paydo (B-155).
                        <br>
                        <br>Bir baytning oʻzida “sarvi guldek”, “qumriyu bulbuldek” deya tashbeh qoʻllash shoirning
                        mahoratidan darak beradi. “Nedur” radifli gʻazalida mahbubani bevosita gulga qiyos qilib,
                        istioradan oʻrinli foydalanadi. Shoir gʻazallarida ham oʻzbek mumtoz adabiyotimizdagi an`anaviy
                        uch obraz - oshiq, mahbuba va raqib uchraydi:
                        <br>
                        <br>Javru zulmlar man zorga, ey gul, ayon etmak nedur,
                        <br>
                        <br>Kim gʻami hajring bila bagʻrimni qon etmak nedur (B-194).
                        <br>
                        <br>Mahbuba misoli bir gul boʻlsa-da, maqsadi oshiqqa azob berish, uni hijron azobida oʻrtashdan
                        iborat. Shoir fikrini davom qildirib, oshiq qalbiga ogʻriq beruvchi savollarga yana murojaat
                        qiladi. Gʻazalda “bulbul kabi ohu figʻon etmak”ni odat qilgan, “falakka oʻz ahvolidin”
                        shikoyatlar etayotgan oshiq dardini yorqin tasvirlab beradi.
                        <br>
                        <br>Ogahiyga payrav tarzida bitilgan “Otashin ruxsorin ul oy ochsa bazm aro” deb boshlanuvchi
                        gʻazalida oshiq yordan madad kutadi. Dastlabki misraning oʻzidayoq istiora va sifatlashga duch
                        kelamiz. “Otashin ruxsor” sifatlashini “ul oy” istiorasiga bogʻlaydi. Yoxud keyingi baytlardagi
                        “toza jon”, “la`li labi” sifatlashlari shoirning badiiy vositalardan oʻrinli foydalanganidan
                        dalolatdir. Gʻazal aruzning yengil, oʻynoqi vazni ramali musammani maqsur bahrida yozilgan:
                        <br>
                        <br>Koʻzi oʻltursa agar, gʻam yoʻqki, topgum toza jon,
                        <br>
                        <br>Aylasa la`li labidin bir boʻsa ehsongina (B-154).
                        <br>
                        <br>Shuningdek, Sodiq gʻazallarida saboga murojaat qilish an`anasi ham uchraydi. Bir gʻazalning
                        oʻzida shoir oshiqning iztiroblarini aniqroq tasvirlash maqsadida “sabo”ga ketma-ket murojaat
                        qiladi. “Sabo” – oshiq dardini mahbubaga yetkazuvchi vosita. “Ey sabo, mandin salom ul sarvari
                        xoʻbona ayt”, “Ey sabo, rahm aylabon ul ofati davrona ayt”, “Ey sabo, yigʻlab sanga arzini der,
                        Sodiq gʻarib” misralari fikrimizni bevosita tasdiqlaydi. Sodiq sabo obrazi orqali nido san`atini
                        qoʻllagan boʻlsa, mazkur misrasida mubolagʻai igʻroqqa murojaat qiladi: “Furqatida qolgʻonim yuz
                        ming tuman armon aro”, – deya aqlan ishonish mumkin boʻlsa-da, oshiq dardini hayotda yuz berishi
                        mumkin boʻlmaydigan darajada kuchaytiradiki, bu gʻazalning ta`sirchanligini oshiradi. Keyingi
                        baytda ma`shuqaga xos sifatlarni ketma-ket sanab, tansiq us-sifot san`atini qoʻllaydi:
                        <br>
                        <br>Vasling istab koʻzlarim doim sirishkafshonligʻ,
                        <br>
                        <br>Ul yuzi gul, sochi sunbul, koʻzlari mastona ayt.
                        <br>
                        <br>Garchi oshiq shu qadar ayriliqu hijronlarga giriftor qolgan boʻlsa-da, “va`dasi yolgʻon
                        mohitobon”, “ofati davron mahliqo jonon”, toʻgʻrirogʻi, “ul rahmsiz sulton”ning vasliga vosil
                        boʻlmoq istaydi.
                        <br>
                        <br>Sodiq she`riyatida “ne sud”, “hanuz”, “istamas”, “arzimas” radifli badiiy ta`sirchan
                        gʻazallarni uchratish mumkin. Ularning har birida lirik qahramon ruhiy olamining muayyan qirrasi
                        yoritiladi. Bu olamda oshiqning iztiroblari, ma`shuqaning “makru hiylalari” turlicha namoyon
                        boʻladi. Shoir bir gʻazalida husni ta`lil san`ati orqali oʻz fikrini chiroyli dalillaydi. Uning
                        fikricha, falakdagi oy ma`shuqaning ruxsori bilan goʻzallik borasida bahsga kirishganda,
                        sharmandalarcha magʻlub boʻladi. Quyosh yor mehri yangligʻ issiqlik baxsh etolmaganidek,
                        sochlarini koʻrgach, sunbul qiynaladi. Bu bilan shoir mubolagʻa san`atini ham ishga soladi:
                        <br>
                        <br>Oy chekib sharmandaligʻ, mehring nuridin oftob,
                        <br>
                        <br>Voy, soching savdosi solmish sunbul a`zosiga tob (B-158).
                        <br>
                        <br>Koʻrib turganingizdek, shoir oy, oftob, sunbul timsollarini yor goʻzalligi bilan qiyoslaydi.
                        Yana bir gʻazalida esa tansiq us-sifotga murojaat qilib, yorga xos xususiyatlarni ketma-ket
                        sanaydi:
                        <br>
                        <br>Oldi koʻnglumni mani bir tiflvash jonongina,
                        <br>
                        <br>Qoshi yo, kipriklari oʻq, koʻzlari fattongina (B-156).
                        <br>
                        <br>Baytdagi qoshni yoy, kiprikni oʻq, koʻzlarni fitna soluvchiga oʻxshatish mumtoz adabiyotda
                        Sodiqqacha boʻlgan an`anadir. Shoir gʻazallarida soʻz oʻyinlari, ma`no nozikliklari bir-biridan
                        goʻzal tasvirlanadi. Mazkur baytda misralarni bir-biriga zid keltirish orqali muqobala
                        san`atining namunasini yaratadi:
                        <br>
                        <br>Baski, noshod aylading Sodiqni hijroning aro,
                        <br>
                        <br>Xushturur emdi visoling ichra boʻlsa komyob (B-158).
                        <br>
                        <br>Yoxud boshqa bir baytida tazod san`ati orqali oshiqning gʻam-alamini tasvirlab, yordan
                        shafqat soʻraydi:
                        <br>
                        <br>Jabri bedoding chekarman kecha-kunduz, ey pari,
                        <br>
                        <br>Bermagil tangri uchun men zorga muncha azob.
                        <br>
                        <br>Shoir gʻazallarida oshiq dardi-yu mahbuba goʻzalligi mubolagʻali tarzda tasvirlanadi.
                        Ma`lumki, mubolagʻada oshiqning gʻamlarini boʻrttirib, yorning goʻzalligini yanada goʻzalroq
                        tasvirlashga erishadi:
                        <br>
                        <br>Orazin koʻrgan kishi xurshidi raxshon istamas,
                        <br>
                        <br>Koʻyida tutgan makon firdavsi rizvon istamas (B-183).
                        <br>
                        <br>Yorning orazi quyoshdan afzal boʻlganidek, ishqida oʻrtanmoqlik jannatdan yaxshiroqdir.
                        Yoxud “qaddi raftorigʻa” koʻngil qoʻygan kishi “sarvi xiromon istamas” yangligʻ bir-biriga
                        qiyosan olingan misralarda istiora, mubolagʻa san`atlarini uygʻunlikda ifodalagan. Shoir tansiq
                        us-sifot san`atini gʻazal boʻylab davom ettiradi. Yorning har bir fazilatini tabiatning bir
                        moʻ`jizasiga qiyos qiladi. Jumladan, “xoli xatti” anbaru rayhonga, “labining shahdi” obi
                        hayvonga oʻxshatilishi yoki “qoshi yoy, koʻzi fatton, kiprigi paykon” singari istioralar bilan
                        maqtalgan pari lirik qahramon uchun huru gʻilmondan afzaldir. Shuningdek, Sodiq she`riyatida
                        Avaz gʻazallariga hamohanglik mavjud boʻlib, uni bevosita “hanuz” radifli gʻazalida
                        uchratishimiz mumkin. Sodiq shoir gʻazallaridagi ayrim soʻzlarning sinonimlarini qoʻllaydi.
                        Jumladan, Avaz gʻazalining dastlabki misrasidagi “Bir nozanin ishqi aro shaydovu maftunman
                        hanuz[6]” misrasi Sodiqda “Bir pari sham`i ruxi hajrida koʻz giryon hanuz” tarzida qoʻllaniladi.
                        Keyingi misralarda esa Avaz gʻazalidagi “ey rafiq” murojaati oʻrnida Sodiq “ey doʻstlar”
                        tarzidagi sinonimni keltiradi. Gʻazal aruzning ramali musammani mahzuf vaznida yozilgan boʻlib,
                        misralar sodda va ravondir. Sodiq gʻazal baytlarida an`anaviy lirikadagi jam` va tafriq
                        san`atiga murojaat qiladi:
                        <br>
                        <br>Doʻstu dushman ishq aro ahvoli zorimni koʻrub,
                        <br>
                        <br>Biri nolondur holimgʻa, biri xandondur hanuz (B-194).
                        <br>
                        <br>Bayoniy gʻazaliyotidagi badiiy mahoratning salmoqli qismi san`atlarni hamda shiru shakar
                        uslubini keng qoʻllashda namoyon boʻladi. Quyida shulardan ayrim namunalar keltiramiz:
                        <br>
                        <br>Talmeh, qaytariq:
                        <br>
                        <br>Majnun Bayoniydek nechun majnunshior oʻlmas gahi,
                        <br>
                        <br>Yurmas junun dashtidagi anduh sahrosi aro (B-528).
                        <br>
                        <br>Savol-javob va shiru shakar:
                        <br>
                        <br>Dedi: ul oy, ne kora sen, guftam havodori shumo.
                        <br>
                        <br>Dedi: nedur matlab sango, guftamki diydori shumo (B-532).
                        <br>
                        <br>Qaytariq va xitob:
                        <br>
                        <br>Rangi ma`ni boʻldi chun nayrangi ma`nidin ayon,
                        <br>
                        <br>Urma nayrang ila hifzi lofi ma`nidorliq (B-545).
                        <br>
                        <br>“Haft shuaro” majmuasida iste`dodli shahzoda shoir Sa`diy she`riyati e`tiborga molikdir.
                        Shoir gʻazallarida goʻzal va ta`sirchan tashbehlar, ohang va badiiyat unsurlari mujassam. Ogahiy
                        gʻazaliga payrav tarzida bitilgan “ustina” radifli gʻazalida fikrlar sodda va ravon ifodalangan
                        boʻlib, unda tashbeh, tazod va talmeh san`atlarini ikki baytda mujassamlashtirib, fikrni
                        mubolagʻali tasvirlasa, boshqa gʻazalida badiiy san`atlarni ketma-ket qoʻllaydi.
                        <br>
                        <br>Shoirning lirik qahramoni ham oshiqdir. U “roʻzu shab” yor ishqida «nolai afgʻon» qilganidan
                        ayanchli ahvolga tushgan. Bir baytning oʻzida tazod, tashbeh, istiora san`atlari, sifatlash
                        vositasini mujassam qiladiki, bu gʻazalning badiiyligini oshiradi:
                        <br>
                        <br>Nolai afgʻon qilurman roʻzu shab bulbul kabi,
                        <br>
                        <br>Gul yuzining furqati ichra boʻlub holim xarob (B-255).
                        <br>
                        <br>Shoir hijronni dashtga qiyos qiladi va istioraning goʻzal namunasini yaratadi. Bu dashtda
                        tashnalikka mubtalo boʻlgan oshiq yorga tomon intiladi. Bu holatdan oshiqni qutqarmoq uchun jomi
                        visol lozim. Aks holda, dashti hijronda oshiqning “bagʻri kabob” boʻlishi tabiiy:
                        <br>
                        <br>Tashnadurman dashti hijroning aro, ey nozanin,
                        <br>
                        <br>Lutf aylab jomi vasling birla qilgʻil komyob.
                        <br>
                        <br>“Laziz” radifli gʻazalida esa oshiq yorning qoʻlidan zahar ichishga ham tayyor. O`zgalarning
                        izzatidan ma`shuqaning jabru jafolari afzal:
                        <br>
                        <br>Zahrni ichsamki dastingdan sani,
                        <br>
                        <br>Ul asalni noʻsh qilgʻondin laziz (B-272).
                        <br>
                        <br>Shoirning fikricha, yorning goʻzalligidan uyalgan quyosh botib ketishni afzal koʻradi. Tun
                        bagʻrini yorib turgan oy esa yor bilan goʻzallik borasida bahslasha olmaganidan tong qa`riga
                        yashirinadi. Bu yangligʻ goʻzallik uchun oshiq “izzati dunyoi dun”dan kechib, firoqu ayriliq
                        azoblariga ham rozi boʻladi.
                        <br>
                        <br>Sa`diy “Boʻlub ul moh mening qatlim uchun bogʻladi bel”, “Xurshidi ruxsoring kelur”
                        misralarida istiora, “Ul qilib javru sitam, soʻng aylabon lutfu karam” kabi satrlarda tazod
                        orqali oshiq-ma`shuqlar ishqini tasvirlaydi. Shuningdek, talmeh san`atini tansiq us-sifot bilan
                        uygʻunlikda qoʻllaydi:
                        <br>
                        <br>Vomiqu Farhod ila Majnun hadisi shoʻxdin,
                        <br>
                        <br>Dam urargʻa muttasil bu oshiqi zor ollida (B-253).
                        <br>
                        <br>Shoir she`rlarida “Lutf etibon, ey sabo, arzim degil yor ollida” singari nido, “Aylagil
                        bir-bir bayon ul mohi ruxsor ollida” kabi mukarrarlar bisyordir. Bunday san`atlar qoʻllanishi
                        misralar badiiyligini oshirishga xizmat qiladi. Shoirning lirik qahramoni tunning intihosini
                        koʻrish ilinjida yashaydi. Uning nazarida, tong visol bilan barobar. Sa`diy she`rlari Laffasiy
                        ta`biri bilan aytganda: “ma`ni jihatdan har bir bayti bir gavhari yakto erdi”.
                        <br>
                        <br>Sa`diyning “yigit” radifli gʻazalida badiiyatning goʻzal namunalari berilgan:
                        <br>
                        <br>Aylamish aqlim adam bir masti beparvo yigit,
                        <br>
                        <br>Gul yuzigʻa bulbuli koʻnglumni ham shaydo yigit, –
                        <br>
                        <br>kabi tashbeh san`ati yoki “Koshki bu tiyra kulbam ravshan etsa gul yuzi” tarzidagi tazodga
                        murojaat fikrimizni isbotlaydi. Shuningdek, shoir “Misli Majnun kezdiribon koʻchai furqat aro”,
                        “Olam ahligʻa boʻlmish dami Iso” kabi talmehlardan oʻrinli foydalanadi. Keyingi baytdagi
                        muqobala san`ati esa shoir fikrini rivojlantirib, ziddiyatni yuzaga keltiradi:
                        <br>
                        <br>Shod etib man notavon koʻnglumni yolgʻon ahd ila,
                        <br>
                        <br>Gʻamligʻ etdi boʻlmay ahdida qadampaymo yigit.
                        <br>
                        <br>“Haft shuaro” majmuasidagi iqtidorli ijodkorlardan biri Otabek toʻra Oqildir. Oqil
                        she`rlarida badiiyat rang-barang shakllarda namoyon boʻladi. Shoir oʻz gʻazallarida oshiq
                        iztirobi, yorning jozibasini tarannum etadi. Oshiqning fikricha, ma`shuqaning goʻzalligi oldida
                        quyosh bir zarradek deya mubolagʻai gʻuluvni tashbeh bilan uygʻunlashtiradi:
                        <br>
                        <br>Mahjabinimning jabinidin xijil ming mohtob,
                        <br>
                        <br>Orazi oʻtrusida bir zarra yangligʻ oftob (B-476).
                        <br>
                        <br>Yoxud gʻazalning keyingi misralarida tansiq us-sifot san`atini tashbihi mafruq bilan birga
                        qoʻllaydi. Ya`ni, yorga xos xususiyatlarni ketma-ket keltirib, ularni oʻzaro farqli ravishda
                        alohida-alohida narsalarga qiyos qiladi. Bayda shoir an`anaviy tashbehlardan oʻrinli
                        foydalanadi:
                        <br>
                        <br>Xatti rayhon, zulfi sunbul, qomati sarvi sihi,
                        <br>
                        <br>La`li jonbaxshu koʻzi qotil, soʻzi durri xushob.
                        <br>
                        <br>Gʻazalning har bir bayti badiiylik kasb etadi. Misralarda fikr aynan takrorlanmaydi. Balki
                        yorning yangi sifatlari ochilib boradi:
                        <br>
                        <br>Noz raxshin sekretib, maydonda boʻlsa jilvagar,
                        <br>
                        <br>Rashk oʻtigʻa mahliqolar jonini qilgʻay kabob.
                        <br>
                        <br>Yorning nozu ishvalarini raxshga – otga oʻxshatadi va istiora san`atini qoʻllaydi. Mahbuba
                        shu darajada goʻzalki, mahliqolar ham rashkdan oʻt boʻlib yonadi.
                        <br>
                        <br>Oqil har bir gʻazalida tashbehning yangi qirralarini kashf etadi:
                        <br>
                        <br>Boshima har balo ishqing aro bitsa gar.
                        <br>
                        <br>Qaytmagʻum hargiz raqib ahlidek aylab shitob (B-477).
                        <br>
                        <br>Oshiq ishq dardidan qanchalik oʻrtansa ham, bu yoʻldan qaytmaydi. Ishq yoʻli mangulik, undan
                        qaytmoq mushkuldir. Keyingi baytda istiora va tashbehni uygʻunlikda qoʻllaydi:
                        <br>
                        <br>Bormagʻil, ey gul, raqib ahlini bazmi sori,
                        <br>
                        <br>Man kabi qilgʻil oni furqat oʻtigʻa kabob.
                        <br>
                        <br>Shoir baytlarida mukarrar san`atiga ham murojaat qiladi:
                        <br>
                        <br>Ayladim onlarga arz ishqida shaydo erkanim,
                        <br>
                        <br>Bir-biridin qildilar lutfni bisyor shoʻx (B-487).
                        <br>
                        <br>Oqil, ayniqsa, tajohuli orif san`atining ustasidir. Atoulloh Husayniyning ta`kidlashicha,
                        “tajohuli orif andin iboratturkim, soʻzlaguvchi bir nimani bilur, ammo bir nuqta bilan oʻzini
                        bilmaganidek koʻrsatur[7]”. Bu san`atga xos lirik qahramon ruhiyatidagi vazminlik, hayratomuz
                        teranlik va ta`sirchan joziba Oqil gʻazallarida ham yorqin ifodalangan. Buni ritorik savol
                        tarzidagi quyidagi gʻazalning toʻla tahlili asosida dalillaymiz:
                        <br>
                        <br>Aqlim olgʻon gul va yo sarvi sanubarmu ekan?
                        <br>
                        <br>Yoʻqsa bir zebo qadu gul yuzli dilbarmu ekan? (B-496)
                        <br>
                        <br>Oqil mumtoz adabiyotimizda an`anaga aylangan tashbehlardan oʻrinli foydalanadi va uni
                        tajohuli orif zamiriga singdiradi:
                        <br>
                        <br>Lablarida zohir oʻlgʻon xatti anbarmudurur,
                        <br>
                        <br>La`l uza yoxud zumurrad birla zevarmu ekan?
                        <br>
                        <br>Yorning ta`rifiga til ojiz, u har qanday maqtovga loyiq, keyingi baytda fikrini davom
                        qildirib, tansiq us-sifot va talmeh san`atlarini birgalikda qoʻllaydi:
                        <br>
                        <br>Musotakallum, mohyuz, anbarxatu ham mushkboʻ,
                        <br>
                        <br>Oshiqkushu jallodxoʻ yori jafokoring kelur.
                        <br>
                        <br>Shoir “bir boqishda muhabbat” aqidasiga amal qilgan holda oʻz fikrini rivojlantiradi va
                        bevosita takrir san`atini qoʻllaydi:
                        <br>
                        <br>Ishq aro oshiq anga man zordek yoʻqtur vale,
                        <br>
                        <br>Garchi oshiqlar ango bisyordur, bisyordur (B-483).
                        <br>
                        <br>Shoir ta`biricha, yorning oshiqlari koʻp boʻlsa-da, bu borada lirik qahramon oʻzini tanhodek
                        his qiladi. Ollohning ta`rifiga bagʻishlangan “paydo” radifli gʻazalida esa amr va nahiy usulini
                        qoʻllab, Ollohga oʻz qalb kechinmalarini bayon qiladi. O`z navbatida, Tangridan istigʻfor
                        soʻraydi:
                        <br>
                        <br>O`tib soʻzi xatosin, yo rab arzini qabul etkil,
                        <br>
                        <br>Senga loyiq sipos ayturgʻa yoʻq Oqilgʻa had paydo.
                        <br>
                        <br>Oqil gʻazaliyoti badiiylikda san`atlar va tasviriy vositalarni birga mujassam etganligi
                        bilan alohida ajralib turadi. Quyidagi baytlar bunga dalildir:
                        <br>
                        <br>Vasl eli har dam topar ermish labidin toza jon,
                        <br>
                        <br>Bilmon ul obi baqomu yoʻqsa kavsarmu ekan?
                        <br>
                        <br>Ne boʻlur Oqil kibi arzimni izhor aylasam,
                        <br>
                        <br>Boqmagʻudek holima ham yori kofirmu ekan?
                        <br>
                        <br>Bu gʻazal ramali musammani mahzuf vaznida boʻlib, taqtesi quyidagicha: - V- - -V - - - V - -
                        - V - . Ishqiy mavzudagi mazkur gʻazal mahbubaning ta`rif-tavsifiga bagʻishlanadi. Payrav
                        boshdan-oxir tajohuli orif san`atiga asoslanib yaratilgan. “Bilib turib bilmaslikka olish”
                        ma`nosini ifodalovchi ushbu she`riy san`at shoirning baytda aks ettirilayotgan biron obrazli
                        iborani aniq aytmasdan oʻzini bilib bilmaslikka olgandek koʻrsatishni nazarda tutadi.
                        <br>
                        <br>Gʻazalning birinchi baytida lirik qahramon aqlimni olgan gulmi yoki sarvi sanuvbarmi yoxud
                        zebo qad, gul yuzli dilbarmu deya sevgilisi ruxsorini gulga, qaddini sarvi sanuvbarga
                        oʻxshatadi. Bunda yuz bilan gul, qad bilan sarv va sanuvbar oʻrtasida qiyosiy munosabatni
                        bilib-bilmaslikka olish shaklida ifodalash natijasida tajohuli orif san`ati hosil qilingan. Ayni
                        shu baytda jam` san`ati ham mavjud. “Jam` narsa va tushunchalarni biron belgi boʻyicha baytda
                        jamlab, ular oʻrtasidagi munosabat asosida biron obrazli fikrni ifodalash san`atidir[8]”. Mazkur
                        baytda mahbuba obrazi bilan gul rang belgisi boʻyicha, qomati bilan sarv va sanuvbar tiklik
                        belgisi boʻyicha jamlanib, ular oʻrtasidagi oʻxshashlik munosabati asosida ta`sirchan badiiy
                        fikr ifodalashga erishilgan.
                        <br>
                        <br>Uchinchi baytda ham tajohuli orif san`ati orqali mahbuba labi jon bagʻishlashda obi baqo
                        (mangu tiriklik suvi) va kavsar (jannat bulogʻi)ga oʻxshashi ta`kidlanadi. Gʻazal maqta`sida ham
                        savol vositasida mahbuba goʻzalligini ta`sirchan ifodalash koʻzga tashlanadi.
                        <br>
                        <br>Koʻrinadiki, gʻazalning har bir baytida biron oʻxshatish keltirilgan boʻlib, shoirning
                        oʻzini bilib bilmaslikka solishi misralar jozibadorligini ta`minlagan.
                        <br>
                        <br>Oqil ham Sodiq va Sultoniy kabi Avaz ijodidan bevosita ilhomlanib ijod qilgan boʻlib, buni
                        shoirning “bila” radifli gʻazalida koʻrishimiz mumkin. Jumladan, Avaz ijodida:
                        <br>
                        <br>Ey kim, tamanno aylama bazm etmak ul jonon bila,
                        <br>
                        <br>Kim uns tutgʻaymu pari suhbat tuzub inson bila[9].
                        <br>
                        <br>Oqilda:
                        <br>
                        <br>Bu kecha bazm etdi kim, bilmon bizi jonon bila.
                        <br>
                        <br>Kim oʻturdi kechani to tonggʻacha afgʻon bila (B-64).
                        <br>
                        <br>Oqil gʻazalning har bir misrasi boshida “Kim” soʻzini qoʻllash bilan leksik takror
                        namunasini yaratadi .
                        <br>
                        <br>Gʻazallar badiiyligini oshirish maqsadida shoirlar qofiya, radif san`ati qoidalariga e`tibor
                        qilishadi. Jumladan, Sa`diyning “oqibat” radifli gʻazalida “parishon, giryon, farovon,
                        oh-afgʻon, pushaymon, shodu xandon” soʻzlari qofiya sifatida tanlangan. Bu qofiyadosh soʻzlarda
                        “n” undoshi raviydir. Raviy harfi qoidaga koʻra qofiyalanuvchi soʻzlar tarkibida aynan
                        takrorlanib kelgan.
                        <br>
                        <br>Sodiqning “istamas” radifli gʻazalida esa qofiyaning zulqofiyatayn – misralarda ikki soʻz
                        ohangdoshligidan foydalanilgan:
                        <br>
                        <br>Kom topmoq orzu qilgʻon labining shahdidin
                        <br>
                        <br>Ranj chekmas, zulmat ichra obi hayvon istamas.
                        <br>
                        <br>Qoshi yoyigʻa nedin mujgon oʻqi solmishdurur,
                        <br>
                        <br>Qatliom etmak gar ul chashmi fatton istamas(B-364).
                        <br>
                        <br>Sultoniyning “koʻzing” radifli gʻazalining bir baytida esa sajli qofiya qoʻllanadi:
                        <br>
                        <br>Behad sitam uzra sitamlar aylabon hajr oʻtigʻa
                        <br>
                        <br>Yoqdi mani subhu maso, ey dilbar siymo, koʻzing(B-282).
                        <br>
                        <br>Shoirning “koʻrgach” radifli gʻazalida qofiyaning musajja turini uchratamiz:
                        <br>
                        <br>Ma`shuqi qilsa bedod, oshiq boʻlur base shod,
                        <br>
                        <br>Oyo, ne holat boʻlgʻay ondin davoni koʻrgach.
                        <br>
                        <br>Sodiqning “hanuz” radifli gʻazalida ham sajli qofiya mavjud:
                        <br>
                        <br>Doʻstu dushman ishq aro ahvoli zorimni koʻrub,
                        <br>
                        <br>Biri nolondur holima, biri xandondur hanuz(B-357).
                        <br>
                        <br>Bu baytda laff va nashr san`atini ham qoʻllab, misralar ohangdorligini oshirishga erishadi.
                        Ya`ni “doʻst – nolon”, “dushman– xandon” sifatida tasvirlanadi.
                        <br>
                        <br>Oqil gʻazallari badiiyati tamomila oʻzgachalik kasb etadi. Shoir “Bir boqib olgʻon mani
                        aqlimni ul dildordur” misrasi bilan boshlanuvchi gʻazalining baytlarida qofiyani takror qoʻllash
                        orqali ta`kidni kuchaytiradi:
                        <br>
                        <br>Husn aro guldek yuziga bulbuloso zorman,
                        <br>
                        <br>Ul pari ruxsori chun gulzordur, gulzor.
                        <br>
                        <br>“Bila” radifli gʻazalida qofiyadosh soʻzlarni raviy sifatida “n” undoshi birlashtiradi.
                        Baytlarda “jonon, afgʻon, qon, jon, ehson, hijron, mahitobon, xandon, giryon, nolon” kabi asosiy
                        ma`no qirralarini boʻrttiruvchi soʻzlar qofiyadosh boʻlib kelgan. Shuningdek, “Kim” soʻzi har
                        misra boshida qoʻllanib, takrir san`atini vujudga keltiradi:
                        <br>
                        <br>Kim qolib ul oydin ayru zulmati hijron aro,
                        <br>
                        <br>Kim qilur bazmini ravshan ul mahitobon bila(B-64).
                        <br>
                        <br>Shu bilan birga, ayrim gʻazallarda qofiyadosh soʻzlarning nomuvofiqligi ham uchraydi.
                        Sa`diyning ba`zi gʻazallaridagi fe`l soʻz turkumiga mansub ayrim soʻzlar toʻliq ohangdosh
                        boʻlolmaydi. Bunday soʻzlarga mayl, shaxs-son yoki zamon qoʻshimchalari qoʻshilib, qisman
                        ohangdoshdek koʻrinadi, xolos. Biroq bu qoʻshimchalarni soʻzdan soqit qilsak, hech qanday
                        qofiyadoshlik boʻlmaydi. Qofiyaning bunday nomuvofiqligi iyto deb yuritiladi[10].
                        <br>
                        <br>Bevafogʻa koʻngul oldurmagʻil hech,
                        <br>
                        <br>Xudo yodin qoʻlingdan bermagʻil hech(B-447).
                        <br>
                        <br>Yoxud keyingi misralardagi “yolbormagʻil, kirmagʻil, doʻndirmagʻil, toydurmagʻil” kabi
                        qofiyadosh soʻzlar bevosita iytoni vujudga keltirgan.
                        <br>
                        <br>Sa`diyning “abas” radifli gʻazalida ham iyto hodisasi mavjud. Fe`l turkumiga mansub soʻzlar
                        harakat nomi shaklida qoʻllanilgan:
                        <br>
                        <br>Beibodat umr oʻtkarmak abas,
                        <br>
                        <br>Dilga gʻayri Olloh kelturmak abas(B-498).
                        <br>
                        <br>Shuningdek, qolgan oʻrinlarda “demak” soʻzlari har ikki baytda qofiya sifatida tanlanadi:
                        <br>
                        <br>Haqni zikri har soʻzingda boʻlmasa,
                        <br>
                        <br>Ul soʻzing jumlasin demak abas.
                        <br>
                        <br>… Sa`diyo, boʻlmasang omil oʻzing,
                        <br>
                        <br>Bu soʻzingni xalqgʻa demak abas.
                        <br>
                        <br>Qolgan ikki baytda esa “etmak” soʻzi qofiyadosh boʻlib kelgan.

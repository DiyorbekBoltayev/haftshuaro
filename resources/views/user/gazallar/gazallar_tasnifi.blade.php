                    <p class="mbr-text mbr-fonts-style display-7">Shahzoda shoirlarning she`riyat janrlariga munosabati
                        an`anaviydir. Ular asrlar davomida shakllangan mumtoz adabiy an`analar ruhida tarbiyalandilar va
                        ularni davom ettirdilar. Zero ular ijodiyotida gʻazallarning asosiy oʻrin egallashi va yetakchi
                        janr ekanligi ham shu bilan dalillanadi. Ikkinchidan, an`anaga koʻra shoir har bir gʻazalni
                        yaratish jarayonida uning matlaini yangidan bitsa ham, keyingi baytlarini matlaga oʻxshatib
                        davom ettirib moslashtirishni, maqtaga taxallusni qistirib ketishni unutmasligi kerak edi.
                        Xususan, ishqiy gʻazallardagi mavzu jihatdan torlikni, an`anaviy tasvir alomatlarining
                        ustunligini, avvalo, shu yoʻl bilan izohlash, ya`ni “bir qolip”da ish tutish oqibati deb qarash
                        mumkin. Shuning uchun ham gʻazal koʻproq shoh adabiy doiralarida keng oʻrin tutadi”[1].
                        Darhaqiqat, bu xususiyatlar toʻlaligicha Feruz saroyidagi “adabiy doira” vakillari – shahzoda
                        shoirlar ijodiga ham aloqadordir. Ular gʻazallaridagi matla` va maqtani qoʻllash hamda an`anaviy
                        tasvir unsurlarining ustuvorligi kabi holatlarda yorqin aks etgan. Boshqacha aytganda, “Haft
                        shuaro”dagi she`rlar janr jihatdan rang-barang boʻlsa ham, ular asosini gʻazallar tashkil etishi
                        shu bilan asoslanadi. Albatta, gʻazallar orasida ayrim sayoz, an`analar doirasidan chiqa
                        olmagan, Feruzga ochiqchasiga xushomad qilib, maqtovni oshirgan namunalar ham bor. Shunga
                        qaramay, shoirlarning voqelikka nisbatan ruhiy munosabatida his-tuygʻularni keng qamrovda
                        ta`sirli ifodalash, ma`naviy-axloqiy qarashlar, jamiyat va tabiatdagi narsa hamda hodisalar
                        xususidagi tasavvurlarni miqyosli aks ettira olishlari ham gʻazalning mavqeini kengaytiradi.
                        <br>
                        <br>Atoqli adabiyotshunos A.Hayitmetov ta`kidlaganidek, “U davr (XIX asr oxiri - XX asr boshi –
                        I.X.) adabiy muhitida lirikada gʻazalchilik yetakchi oʻrinda turar, hamma gʻazal yozish bilan
                        she`riy iste`dodini sinar edi”[2].
                        <br>
                        <br>Gʻazallarda shoirlar hayotida va atrof-muhitda yuz bergan real hodisalarga ishora qiluvchi
                        voqeabandlik turlichadir. Ular mohiyatan quyidagi mavzularda namoyon boʻladi: zamondoshlar bilan
                        muayyan munosabatlar tufayli yozilgan gʻazal; saroy bazmi va adabiy anjumanlarga bagʻishlangan
                        gʻazallar; soz va ustoz san`atkorlar hamda shoirlar haqidagi gʻazallar va Feruzni sharaflovchi
                        gʻazallar.
                        <br>
                        <br>Majmuadagi gʻazallarni guruhlash yuqoridagi xususiyatlarni inobatga olishni taqozo etadi.
                        Shu bois, nazarimizda, gʻazallarni shakl va mazmun–mohiyat jihatdan quyidagicha guruhlash
                        mumkin:
                        <br>
                        <br>1. Shakl va yaratilish xarakteriga koʻra:
                        <br>
                        <br>a) muxtare gʻazallar; b) payrav gʻazallar.
                        <br>
                        <br>2. Mavzu va gʻoyaviy yoʻnalishiga koʻra:
                        <br>
                        <br>a) ishqiy gʻazallar;
                        <br>
                        <br>b) tasavvufiy-ta`limiy gʻazallar;
                        <br>
                        <br>v) diniy-ma`rifiy gʻazallar.
                        <br>
                        <br>Gʻazallar tizimidagi bu ikki xil yoʻnalishga xos bir jihatni alohida ta`kidlash kerak. Bu
                        shahzoda shoirlar gʻazaliyotidagi mumtoz an`analarning ustuvorligi she`rlarning ichki
                        strukturasiga nisbatan koʻproq umumiy mazmun-mundarija, lirik qahramon, ishqiy mavzu,
                        badiiy–tasviriy vositalar va boshqa poetik unsurlarda namoyon boʻlishidir. Shunga qaramay, bu
                        mushtaraklik har bir shoir va gʻazalning oʻziga xosligini, muayyan mustaqil yondashuv hamda
                        badiiy-ijodiy hissalarini inkor eta olmaydi. Binobarin, “Sharq she`riyati an`anaviy she`riyat
                        boʻlib, uning daxlsiz an`anaviy shart, qoida, talab va xususiyatlari ayon boʻlsa ham, an`ana
                        iste`dodni tarbiyalaydi, did, saviya, imkoniyatlarining ochilishi va kengayishiga behad ta`sir
                        koʻrsatadi”[3]. Zero, bu ta`sir majmuadagi gʻazallarning an`analarni izchil davom ettirgani,
                        ularning qator qirralarini yanada boyitganlari bilan adabiy-estetik ahamiyatga molikdir.
                        Gʻazallarning majmuadagi she`rlarga nisbatida quyidagi jihatlari yaqqol koʻzga tashlanadi:
                        <br>
                        <br>1. Yetti nafar shoirning ham she`rlari gʻazal bilan boshlanadi va koʻpchilikni tashkil
                        etadi;
                        <br>
                        <br>2. Gʻazallarda ishqiy mavzu ustuvorlik qiladi.
                        <br>
                        <br>Bu davr gʻazaliyotiga xos ikki xususiyatni ta`kidlash joiz:
                        <br>
                        <br>I. Gʻazaliyotda adabiyotshunos Yo.Ishoqov qayd etganidek, oʻzbek mumtoz she`riyatidagi
                        turkona, mumtoz va oliy navoiyona uslublarga munosabatda navoiyona uslub keng qoʻllanildi.
                        Chunki “bu uslubning asosiy xususiyatlari: gʻazal va qasidaning yetakchi mavqe egallashi, gʻazal
                        mavzusining boyishi, poetikaning takomillashuvi” boʻlib, uning shakllanishi Rabgʻuziy va
                        Xorazmiy davridan oldinroq boshlangan[4].
                        <br>
                        <br>II. Feruz gʻazallariga yozilgan payravlar davr gʻazaliyotida koʻp uchraydi. Payrav
                        imkoniyatlaridan ham ustalik bilan foydalanilib, ularga koʻp hollarda ijtimoiy tuzumdan
                        norozilik va johillikni qoralash motivlari singdirib yuboriladiki, bu hol muxtare gʻazallardagi
                        ma`rifiy, axloqiy qarashlarni mantiqan boyitadi.
                        <br>
                        <br>Majmuaning dastlabki qismidagi yigirma gʻazal Murodiy qalamiga mansub boʻlib, aksariyati
                        oshiqona motivlardan, ayrimlari esa Feruz madhidan iborat. Ular muallifning zullisonayn shoir,
                        ijodiy qobiliyat sohibi, “ma`no gulzori ichra fikri xurdabin, mukarram zot” (Bayoniy) ekanidan
                        guvohlik beradi. Afsuski, shoʻro davrida Murodiy “saroy maddohi” sifatida kamsitildi, xususan,
                        gʻazallari bir yoqlama talqin etildi. Adabiyotshunos N.Qobulov Murodiyni Xiva xonini
                        ideallashtirgan shoir deyish bilan birga, gʻazallarida “asosiy tema insoniy his-tuygʻular, sevgi
                        va muhabbat masalasi pornografiya qobigʻiga oʻralgan holda talqin qilinadi”, – deb yozadi va
                        “kim erkan?” radifli gʻazalini misol qilib keltiradi[5]:
                        <br>
                        <br>Noz ila dedi tun kecha yori suxandon,
                        <br>
                        <br>Ishq ichra Murodiy kabi mardona kim erkan?
                        <br>
                        <br>Vaholanki, mazkur baytda an`anaviy oshiqning ma`shuqa iltifotidan hayratlanish lavhasi
                        ta`sirli aks etgan.
                        <br>
                        <br>Ma`lumki, gʻazal asosan ishq-muhabbat masalalariga bagʻishlangan janrdir. Adabiyotshunos
                        M.Pirnazarov ta`kidlashicha, “Turkiy xalqlar poeziyasining yangi davr (XX asr boshlari – I.X.)
                        ijodkorlari buyuk salaflarning qalb daftaridan bahramand boʻlgani holda oʻz gʻazallarida
                        insonni, uning muqaddas tuygʻusi – real ishqni kuyladilar, olijanob obraz – oshiq obrazini yangi
                        original bir tarzda tarannum etishga intildilar”[6].
                        <br>
                        <br>Darhaqiqat, muhabbat – muqaddas tuygʻu va an`anaviy mavzu. Biroq, har bir shoir ijodida bu
                        mavzuning yangi qirralari kashf etiladi. Bu jihatdan Murodiy gʻazaliyoti ham oʻziga xosdir.
                        Shoir ishqni ta`riflar ekan, unga insoniy fazilat sifatida qaraydi va sevishganlar qalbida bu
                        tuygʻu ifodasini koʻrishni istaydi.
                        <br>
                        <br>Murodiy ijodidagi asosiy lirik qahramon – oshiq. U tunu kun goʻzal yor hajrida oʻrtanadi.
                        Qalbidagi tuygʻuni mahbubaga izhor qilish lirik qahramonga mushkul. Buni shoirning quyidagi
                        misralarida koʻrish mumkin:
                        <br>
                        <br>Vah, netaykim, ayta olmam sirrim ul yor ollida,
                        <br>
                        <br>Ey sabo, qil arzim ul la`li shakarbor ollida[7].
                        <br>
                        <br>Gʻazalning matla`sidagi saboga murojaat oxirigacha davom etadi.
                        <br>
                        <br>Vahki, ul mahvash mango jabru jafosin koʻb qilur,
                        <br>
                        <br>Har necha qilsam niyoz ajz izhor ollida (B-3).
                        <br>
                        <br>Murodiyning poetik mahoratini fors-tojik tilidagi gʻazallarida ham koʻrish mumkin.
                        <br>
                        <br>Shoir muhabbatni tarannum etuvchi bir gʻazalida oshiq tilidan ma`shuqa goʻzalligini
                        ta`riflar ekan, yor qiyofasini mohtob, sarv, gulshan, sunbuli rayhonga qiyoslash orqali aniq
                        tasvirlaydi. Bu goʻzal narsalar ham ma`shuqa goʻzalligi oldida oddiy bir narsa ekanligini
                        ta`kidlaydi. Gʻazal badiiyligini oshirish maqsadida mumtoz adabiyotdagi an`anaviy timsollardan
                        foydalanadi.
                        <br>
                        <br>Majmuadagi oʻn yetti gʻazal Farrux qalamiga mansubdir. Shoir she`rlariga akademik
                        V.Abdullayev tanqidiy munosabatda boʻladi. Olim “Navoiy va bir davrning ikki shoiri” maqolasida
                        ta`kidlashicha, “Farrux adabiyot rivojiga hech narsa qoʻshmaydi, ermak bilan shugʻullanadi-yu
                        shu bilan nom yoyish, oʻz hokimlik imtiyozini oshirishni maqsad qilib qoʻyadi”[8]. Mafkura
                        muvofiq ifodalangan bu fikrning isboti sifatida quyidagi baytni keltiradi:
                        <br>
                        <br>Qilur el bu soʻzung chin gumonu ta`nazan Farrux,
                        <br>
                        <br>Sango ne kizb ila anchakli joningga azob aylab.
                        <br>
                        <br>Farrux haqida dastlabki fasllarda yetarlicha munosabat bildirilgani bois shoir she`rlarini
                        tahlilga tortmadik.
                        <br>
                        <br>Xorazm adabiy muhitining iste`dodli ijodkorlaridan biri Sultoniydir. Majmuada dastlab
                        shoirning gʻazallari arab alifbosi tartibida joylashtirilgan. Shoir gʻazallarining aksariyatida
                        ishq-muhabbat mavzusi yetakchilik qiladi va muxtare gʻazallar alohida oʻrin egallaydi. Shoirning
                        oshiqona gʻazallaridagi obrazlar an`anaviydir: oshiq, mahbuba va agʻyor (raqib). Ular talqinida
                        esa shoirning ijodiy salohiyati aks etadi. Jumladan, Sultoniyning lirik qahramoni ishq dardiga
                        mubtalo oshiq va oʻz ahdida sobitdir. U yor vasliga yetmoq ilinjida har qanday sitamlarni
                        yengishga qodir:
                        <br>
                        <br>Umidi lutf etarman ul mahliqoni koʻrgach,
                        <br>
                        <br>Ul qilgʻusi sitamlar man mubtaloni koʻrgach.
                        <br>
                        <br>... Qolgʻaymu sabru toqat, ahli jahonda, ahbob,
                        <br>
                        <br>Ul shoʻxi mahliqodin nozu iboni koʻrgach (B-39).
                        <br>
                        <br>Keyingi gʻazal esa, bevosita yuqoridagi misralarning mantiqiy davomidir. Oshiqning umri
                        hijronda oʻtadi, yorga – visolga yetmoqqa fursat ham, imkoniyat ham yoʻq. Oshiq chekkan dardu
                        mehnat, boshiga tushgan ofat yanada cheksiz. Qator baytlarida gʻazalning mohiyati ochila boradi:
                        <br>
                        <br>Aylasa lutfu muruvvat ul mohi shirinliqo,
                        <br>
                        <br>Hajr anduhi aro koʻzumni giryon aylagach (B-39).
                        <br>
                        <br>Adabiyotshunos V.Abdullayev Sultoniyning “oʻxshaydur”, “yigit” radifli gʻazallari
                        toʻgʻrisida mulohaza yuritib, “shoir Navoiyga ijodiy izdoshlik mezonini buzgan”, – degan
                        xulosaga keladi. Jumladan, Sultoniyning:
                        <br>
                        <br>Kel, ey Sultoniy, endi ul pari ishqini tark etgil,
                        <br>
                        <br>Oning vasli gadovu shahgʻa yakson oʻlgʻon oʻxshaydur (B-42), –
                        <br>
                        <br>bayti “mumtoz adabiyotda shohu gado ishq oldida tengdir, deb kelingan an`anaga xilofdir”, –
                        deb hisoblaydi.
                        <br>
                        <br>Bizningcha, ishq oldida barchaning tengligi xususidagi fikr an`anaviy tarzda ifodalangan.
                        <br>
                        <br>Ma`lumki, Navoiyning navosi oʻzidan keyingi barcha ijodkorlarga saboq bergan, ularga ustoz
                        ijodxonasi sifatida bagʻrini ochgan. Bu navodan bahra olmagan shoirning oʻzi boʻlmasa kerak.
                        Sultoniy ham ana shunday ijodkorlardan biri sifatida Navoiyga hamohang tarzda oʻz
                        orzu-istaklarini, qalb kechinmalarini bayon etadi. Jumladan, “yigit” radifli gʻazalning
                        yaratilishi ham ijodiy ergashishning namunasidir. Zero, “yigit” radifli gʻazalni Navoiy ijodida
                        ham uchratamiz:
                        <br>
                        <br>Koʻnglim oldi bir paripaykar malaksiymo yigit,
                        <br>
                        <br>Kim, bani odamda andoq boʻlmamish paydo yigit[9].
                        <br>
                        <br>Yoki boshqa bir gʻazalida:
                        <br>
                        <br>Gah qilur zulmu gahe qatl havas kim koʻrmish,
                        <br>
                        <br>Qotilu zolim aro boʻyla havasnok yigit[10].
                        <br>
                        <br>Sultoniy bu radifni saqlagan holda Navoiy qarashlaridagi “yigit” soʻzini yangi ma`nosi bilan
                        davom ettiradi:
                        <br>
                        <br>Aylagach yuz uzra zulfin anbarafshon ul yigit,
                        <br>
                        <br>Ikki zulfin yuz uza qildi parishon ul yigit (B-94).
                        <br>
                        <br>O`z-oʻzidan ma`lumki, mumtoz she`riyatda “yigit” soʻzi jinsga emas, yoshga nisbatan
                        qoʻllangan. Kishining balogʻatga yetgan davri yigitlik davri deb yuritilgan. Jumladan, Rabgʻuziy
                        oʻzining “Qisasi Rabgʻuziy” asarida: “Zulayho yigit erdi” deganida, uning balogʻatga yetganiga
                        ishora qilgani ham buning bevosita dalilidir[11].
                        <br>
                        <br>Sultoniy “yigit” radifli gʻazali orqali ishqning yangi qirralarini ochib beradi:
                        <br>
                        <br>Hasrat etsam, doʻstlar, husniga oning, ayb emas,
                        <br>
                        <br>Ishq elini ayladi husniga hayron ul yigit (B-94).
                        <br>
                        <br>Shuningdek, Sultoniy Ogahiy lirik an`analarining ham munosib davomchisidir. U ustozga
                        ergashar ekan, “tarhi toza” nazm yaratishga, oʻziga xos poetik ma`no qirralari bilan an`anani
                        boyitishga intiladi. Uning “sallamno”, “olam gado, men ham gado” radifli gʻazallari buning
                        yorqin dalilidir. Sultoniyning quyidagi bayti mumtoz she`riyatda keng tarqalgan tashbeh va
                        tashxisga ijodiy yondashuv namunasi boʻlib, ogahiyona ohangga uygʻundir:
                        <br>
                        <br>Ajab sayyod ekansan, chustu chobuk bir nukta birla
                        <br>
                        <br>Boqib men zorni koʻnglum olgʻoningga sallamno.
                        <br>
                        <br>Sultoniyning aksariyat gʻazallari oʻynoqi uslubi, musiqiy ohangi, timsollar rang-barangligi
                        va badiiy boʻyoqdorligi bilan ham ajralib turadi. Jumladan, Ogahiyning eng mashhur gʻazallaridan
                        biriga Sultoniy bitgan tatabbu` mazmun va shakl jihatdan salaf shoir fikrlarining davomiday
                        jaranglaydi:
                        <br>
                        <br>O`lgan badanga jon berur la`ling aro shirin soʻzing,
                        <br>
                        <br>Isosifat guftoringa olam gado, men ham gado,
                        <br>
                        <br>Bogʻi Eram gulzoridek oʻlmas yuzing gulzoriga,
                        <br>
                        <br>Ey mahliqo, gulzoringa olam gado, men ham gado (B-87).
                        <br>
                        <br>Keyingi baytda “gulzor” soʻzining uch marta takrori orqali asosiy ma`no kuchaytirilgan va
                        mahbuba yuzining goʻzalligi boʻrttirib tasvirlangan. Shoir an`anaviy tashbehga murojaat etib,
                        yorning goʻzal chehrasini Bogʻi Eram gulzoriga qiyos qiladi.
                        <br>
                        <br>Majmuadan oʻrin olgan Sodiq gʻazallarida muhabbat mavzusi oʻzgachalik kasb etadi. Bunda
                        oshiqning yorga murojaati an`anasi lirik qahramonning qalb iztiroblarini bevosita mahbubaning
                        oʻziga bayon etish shaklida uchraydi:
                        <br>
                        <br>Hijron aro muncha mani, ey chashmi xummor, oʻrtama,
                        <br>
                        <br>Tigʻi gʻaming birla qilib koʻksumni afgor oʻrtama (B-172).
                        <br>
                        <br>Boshqa bir gʻazalidagi qator baytlarda esa mahbubaga savollar orqali uning oshiqqa jabru
                        sitami, hatto qotillikka intilish lirik qahramon ruhiy holatiga ta`sirini quyidagi bayt
                        ishonarli ifodalaydi:
                        <br>
                        <br>Maqsading gar ishq elin qatl etmoq esa, ey sanam,
                        <br>
                        <br>Kipriging oʻq aylabon, qoshing kamon etmak nedur.(B-194)
                        <br>
                        <br>Bu oʻrinda kiprikning oʻqqa, qoshning kamonga nisbat berilishi an`anaviy boʻlsa ham,
                        baytdagi ma`noni kuchaytirishga xizmat qilgan. Shoir an`anaviy tashbehlardan foydalanadi,
                        gʻazalda yangi fikr ifodalamaydi. An`anaga koʻra, shoir misralaridagi ma`shuqa jabr qiluvchi, u
                        oshiq qalbini oʻrtaydi. Biroq oshiq unga har lahzada intiladi. Chunki ma`shuqa – goʻzallik va
                        orzu timsoli. Lekin ba`zan zolim, bevafo va va`dasini unutadi. Quyidagi baytda shunday
                        ma`shuqaga nisbatan oshiq munosabati aks etgan:
                        <br>
                        <br>Aylay olmassan vafo, avvalda yolgʻon va`dalar,
                        <br>
                        <br>Qilmagʻay erding mango, ey javri jafo koshki.
                        <br>
                        <br>Garchi oshiqni shu qadar ayriliqu hijronlarga giriftor qilgan boʻlsa-da, “ofati davron
                        mahliqo jonon”, toʻgʻrirogʻi, “ul rahmsiz sulton”ning vasliga yetmoq oshiqning orzusidir.
                        <br>
                        <br>Sa`diy ijodidagi muhabbat mavzusi talqini ravon va ta`sirchan ifodasi bilan ajralib turadi.
                        Shoir ma`shuqaning tashqi chiroyi bilan botiniy goʻzalligini, boy ma`naviy olamini bir butun
                        holda tasvirlashga koʻproq e`tibor beradi va undan chinakam sadoqat kutadi hamda uning
                        shafqatini har narsadan aziz koʻradi:
                        <br>
                        <br>Gul yuzing nazzorasi jondin laziz,
                        <br>
                        <br>Ostoningdur gulistondin laziz (B-264).
                        <br>
                        <br>Ammo har doim oshiq oʻz orzusiga oson erisha olmaydi. Ishq yoʻlidagi qiyinchiliklar uni
                        toblaydi, iztiroblarga chidashgagina emas, ularni yengishga oʻrgatadi. Nihoyat “ertadan
                        kechgacha” yor ishqida “bulbul kabi nola chekkan” va hijronaro “holi xarob” boʻlsa ham, bariga
                        bardosh topgan oshiq ma`shuqadan marhamat kutadi:
                        <br>
                        <br>Tashnadurman dashti hijroning aro, ey nozanin,
                        <br>
                        <br>Lutf aylab jomi vasling birla qilgʻil komyob (B-255).
                        <br>
                        <br>Oqil gʻazallaridagi ishq samimiyligi bilan alohidalik kasb etadi. Shoirning lirik qahramoni
                        ma`shuqani bir koʻrishdayoq “aqlini olgan, otash ruxsori joniga oʻt solgani” tufayli sevib
                        qolgan. Lekin uning oshiqlari bisyor. Shu tufayli lirik qahramon oʻz sadoqati bilan raqiblardan
                        ustun boʻlishga, ishqda raqobatdan qoʻrqmaslikka intiladi:
                        <br>
                        <br>Ishq aro oshiq anga man zordek yoʻqtur vale,
                        <br>
                        <br>Garchi oshiqlar anga bisyordur, bisyor (B-480).
                        <br>
                        <br>Albatta, yor yoʻlidagi firoq ayyomi, ma`shuqa jamolini koʻrishga intiqlik va visolga
                        intilish motivlari Oqil gʻazaliyotiga ham xos. Shu bilan birga, u “dil” va “koʻngil” timsollari
                        orqali ularga oʻzgacha talqin beradi. Jumladan, bir gʻazalida esa oshiq oʻz koʻnglidan suyunchi
                        soʻraydi. Chunki yor tashrifi oshiq uchun kutilmagan xursandchilik, uning vaslini koʻrishga
                        muyassar boʻlish esa katta baxt:
                        <br>
                        <br>Ey dil, suyunchi ber, bukun bazmingga dildoring kelur,
                        <br>
                        <br>Har necha ta`rif aylasang, boʻlgʻon sazovoring kelur (B-481).
                        <br>
                        <br>Majmuadagi gʻazallarning 348 tadan iborat salmoqli qismi Muhammad Yusufbek Bayoniy qalamiga
                        mansubdir. O`zbek va fors tillarida bitilgan bu gʻazallar mumtoz gʻazaliyotning sara namunalari
                        boʻlib, gʻoyaviy-mazmuniy yuksakligi va badiiy goʻzalligi bilan ajralib turadi. Bu masalalar
                        adabiyotshunosligimizda I.Adizova, N.Jumaxoʻja va V. Abdul-layevlarning tadqiqotlarida batafsil
                        yoritilgan[12]. Shu sababli bu xususda ayrim jihatlarni alohida ta`kidlash bilan cheklanamiz.
                        <br>
                        <br>Bayoniy gʻazaliyotidagi muhim jihatlardan biri unda ilohiy va zaminiy ishqning oʻzaro
                        uygʻunlikda tasvirlanishidir. Zero, shoir dunyoviy ishqqa koʻproq moyillik koʻrsatar ekan, yor
                        qomatining sarvga qiyoslanishi bilan qanoatlanmaydi, balki chinakam goʻzallikni ma`shuqa
                        odobida, zeboligida koʻradi va an`anani boyitgan holda yor qaddiga yangicha ta`rif beradi:
                        <br>
                        <br>Gulshanda agar shamshod ra`no esa ham harchand,
                        <br>
                        <br>Zeboliq aro ermas yorim qadigʻa monand (B-293).
                        <br>
                        <br>Ammo lirik qahramon uchun yorning botiniy dunyosi, vafodorligi muhimdir. Shu bois yor
                        qanchalik goʻzal boʻlsa ham, uning ahdu paymonga sadoqati yanada laziz:
                        <br>
                        <br>Hech nima ermas vafoyi ahdu paymondek laziz,
                        <br>
                        <br>Ya`ni mahjurigʻa boʻlgʻan vasli jonondek laziz (B-296).
                        <br>
                        <br>Majmuada tasavvufiy-orifona gʻazallar ham mavjud. Bu esa shoirlarning diniy va dunyoviy
                        ilmlardan xabardorligini anglatadi. Ular olam va odamning yaratilishi Olloh nomi bilan bogʻliq
                        ekanligini inkor etishmaydi, oʻz she`rlarida soqiy, zulf, jom, fano ahli, iymon, may, dahr,
                        faqr, zohid, bazm, qanoat kabi qator tasavvufiy istiloh va timsollarni qoʻllab, orifona gʻazal
                        va baytlar bitishgan.
                        <br>
                        <br>Majmuadan Sultoniy, Oqil va Bayoniyga tegishli hamdlar oʻrin olgan. Unda Sultoniy
                        Parvardigorning olamni yaratish va yashnatishdagi qudratini ta`riflar ekan, “Zihi Qodirki,
                        sun`idin qilib arzu samo paydo” deydi. Ma`lumki, “sun`” soʻzi “yasash, yaratish, ish, hunar,
                        mahorat” ma`nolarini anglatadi. Sone` – Ollohga xos xususiyat. Shoir bu gʻazalda Ollohning
                        qudrati bilan yeru osmon, “jahon bogʻi” va dunyo goʻzalliklari yaratilganligini ta`kidlaydi.
                        <br>
                        <br>Gʻazalning maqtasida shoir Olloh hamdi boʻstonida bisyor navolar paydo boʻlishiga umidvor
                        boʻlib, unga murojaat etadi, ijodiy ragʻbat soʻraydi:
                        <br>
                        <br>Ilohi, ayla Sultoniy tilin bulbul kibi goʻyo
                        <br>
                        <br>Anga hamding gulistonida boʻlsun koʻp navo paydo (B-35).
                        <br>
                        <br>Sultoniy gʻazaliyotida Muhammad (s.a.v.) – Rasulullohga bagʻishlangan bir na`t bor. Unda
                        paygʻambarimizning zohiriy va botiniy sifatlari tilga olinadi va quyidagi matla` keltiriladi:
                        <br>
                        <br>Yo Rasululloh, karam behaddu bepoyon etib,
                        <br>
                        <br>Haq seni qildi jami anbiyogʻa peshvo (B-35).
                        <br>
                        <br>Koʻrinadiki, bu oʻrinda Yaratuvchining Paygʻambarga ato etgan ikki xislati – marhamatu
                        saxovatining cheksizligi va barcha aziz paygʻambarlarga rahnamo (peshvo) boʻlishi qayd etiladi.
                        <br>
                        <br>Sa`diyning tasavvufiy qarashlarida Ollohga va paygʻambarga munojot keng qoʻllanilgan. U bir
                        gʻazalida Yaratganga iltijo qilar ekan, gunohlari tufayli vayrona qalbini nafsdan xalos etib,
                        qanoat berishni, gʻamgin dilni shod, oʻzini doʻzaxdan ozod etishni soʻraydi. Chunki “qanoat faqr
                        yoʻliga kirib, hayvoniy nafslar, keraksiz, yomon xulq-odatlardan qutulib, oʻzini xokisorlik,
                        kamtarinlik rutbasiga solishdir”[13]:
                        <br>
                        <br>Xudoyo, san qilibsan koʻnglumi shod,
                        <br>
                        <br>Tanimni aylagil doʻzaxdin ozod.
                        <br>
                        <br>Manga yo Mustafo aylang shafoat,
                        <br>
                        <br>Qilib jurmimni lutfing birla barbod (B-258).
                        <br>
                        <br>Biroq gʻazal mohiyatidagi mazmun soʻfiylar odatiga xilofdir. Ma`lumki, tasavvuf ta`limotiga
                        amal qiluvchilarning Olloh ishqi yoʻlida chekkan iztiroblari oldida doʻzax azoblari hech narsa
                        emas. Shoir esa doʻzax azoblaridan vahimaga tushib, Ollohdan gunohlarini kechirishni soʻraydi.
                        <br>
                        <br>Bayoniyning orifona qarashlari bevosita ilm-ma`rifat bilan bogʻliq. U oʻzining forscha bir
                        gʻazalida koʻp va yolgʻon soʻzlovchilar bilan suhbatdosh boʻlmaslikni ta`kidlaydi:
                        <br>
                        <br>Bo harzagoʻyon zinhor, ey zor dil, suhbat mador (B-352).
                        <br>
                        <br>Yana bir gʻazalida esa oʻz she`rlarini ma`rifatsiz kimsalardan uzoq tutish kerakligini
                        uqtiradi:
                        <br>
                        <br>Az olami bema`rifat she`rat Bayoniy dur dor,
                        <br>
                        <br>Oxundi abtar yak taraf, mardi suxanvar yak taraf (B-425).
                        <br>
                        <br>Ma`nosi:
                        <br>
                        <br>Bayoniy, ma`rifatsiz olamdan she`ringni uzoq tut,
                        <br>
                        <br>Pastkash oxund boshqa-yu, mardi suxanvar boshqa.
                        <br>
                        <br>Majmuada gʻazallar tarkibida chuqur ijtimoiy mazmunga ega boʻlgan gʻazallar ham anchagina.
                        Ular aynan shu mavzudagi baytlarda bevosita, ishqiy gʻazallarda esa bilvosita (koʻpincha
                        xotimada) ifoda etiladi. Mavjud zamon va ahli davrondan, bevafo yor va riyolardan qattiq
                        shikoyat ruhidagi bu gʻazallar aksari shoirlar hayotining ba`zi tafsilotlariga (nopok
                        amaldorlardan nolish) ham ishora etadi. “Zulm ahli” va “nokas”lar bunday gʻazallarda asosiy
                        darajaga koʻtariladi. Lekin ijtimoiy gʻazallarda Avaz va Faqiriy gʻazaliyotiga xos isyonkor
                        ruhga nisbatan nolish, zorlanish ohanglari ustuvorlik qiladi. Ikkinchidan, ijtimoiy muammolarga
                        munosabat ifodasi aks etgan gʻazallarda, koʻpincha, gʻurbat mazmunidagi ohanglar boʻrtib turadi.
                        <br>
                        <br>Oqilning “aylading” radifli gʻazalida nolinish holatiga duch kelamiz. Gʻazal Ogahiyga payrav
                        tarzida bitilgan. Biroq unda shoir Ogahiy fikrlariga shunchaki hamohanglik bildiradi. Jumladan,
                        Ogahiyda oʻqiymiz
                        <br>
                        <br>Ey falak, nettim seni dushmanligʻ izhor aylading,
                        <br>
                        <br>Notavon koʻnglum tuganmas gʻamga duchor aylading.
                        <br>
                        <br>Oqilda bu misralar yangicha talqinda ifodalanadi. Ya`ni Ogahiy misralarida gʻam-qaygʻu
                        asosiy oʻrinda boʻlsa, Oqil she`rida hijron, ayriliq motivlari yetakchilik qiladi:
                        <br>
                        <br>Kunlarim doim oʻtirdim hajr oqshomi bila,
                        <br>
                        <br>Ey falak, nettim seni dushmanligʻ izhor aylading.
                        <br>
                        <br>Ma`lumki, majmua yaratilgan davrda Xorazm adabiy muhitida payravchilik an`anasi rivojlandi.
                        Payrav bitish muayyan shoirning iziga tushish, undan ijodiy oʻrganish va, oʻz navbatida,
                        mahoratini sinab koʻrish san`atidir. Shu bilan birga, payrav ijodiy musobaqa, bellashish va
                        badiiy mahorat sohasida oʻz qalami qudratini sinash boʻlib, salaf va xalaf shoir munosabatida
                        aks etadi.
                        <br>
                        <br>“Haft shuaro” majmuasidan oʻrin olgan har bir shoir ijodida payrav gʻazallarga duch kelamiz.
                        Payravlarning aksariyati Feruz gʻazallariga bogʻlangan. Bu tabiiy hol. Chunki Feruz, Bayoniy
                        ta`kidlaganidek, adabiy suhbat tuzganda, oʻz gʻazallariga payrav bitishni tayinlardi. Bu,
                        avvalo, shoirlarni koʻproq ijod qilishga, ragʻbatga undasa, ikkinchidan, gʻazalning yangi
                        namunalari vujudga kelishiga xizmat qildi.
                        <br>
                        <br>Manbadagi shoirlar ijodida Feruzning “paydo” radifli gʻazaliga bitilgan payravlar uchraydi.
                        Biroq har bir shoir qalamiga mansub payrav oʻziga xos mazmun va mohiyat kasb etadi. “Paydo”
                        radifli payravlarning barchasida dunyo va uning yaratilishi toʻgʻrisida fikr yuritiladi.
                        N.Jumaxoʻja ta`kidlaganidek: “Feruz dunyoqarashida dunyoviylik, hayotsevarlik ruhi ustuvor.
                        Ammo, shu bilan birga, Feruz bagʻoyat taqvodor inson edi. U oʻziga nasib etgan dunyoning mulki,
                        ne`matlari, iste`dod qudrati – bari-barini Ollohning inoyati, xohish, irodasi deb
                        tushunadi”[14]. Buni bevosita Feruzning “paydo” radifli gʻazalida koʻrishimiz mumkin:
                        <br>
                        <br>Zihi bordur zuhuringga azal birla abad paydo,
                        <br>
                        <br>Tafakkur aylabon zoting, qila olmas xirad paydo.
                        <br>
                        <br>...Borisi xohishi taqdiring ila boʻlgʻusi mavjud,
                        <br>
                        <br>Nekim gar bandadin olamda boʻlsa neku bad paydo[15].
                        <br>
                        <br>“Paydo” radifli gʻazalni biz Navoiy, Fuzuliy, Ogahiy she`riyatida ham uchratamiz. Ma`lumki,
                        Feruz va uning avlodlari bevosita ustoz ijodkorlardan ilhomlanib, Ollohning yaratuvchilik
                        qudrati, uning sifatlarini oʻz iste`dodi nuqtai nazaridan ta`riflashga intilganlar.
                        <br>
                        <br>Hukmdor shoir ilohiy qudrat oldida oʻzining notavonligini his qiladi. Feruz gʻazalida
                        oʻrtaga tashlangan gʻoyani shahzoda shoirlar bevosita davom qildiradi. Jumladan, Sodiq bu
                        gʻazalni quyidagicha yaratadi:
                        <br>
                        <br>Zihi xoliqqa boʻlmish san`ati birlan jinon paydo,
                        <br>
                        <br>Jahon ichra vuhushu tuyur birla insu jon paydo.
                        <br>
                        <br>Ajab qudrat erurkim, xalq aylab ushbu olamni,
                        <br>
                        <br>Ham ul mehmonsaroda qildi necha mehmon paydo (B-155).
                        <br>
                        <br>Shoir nazarida Yaratuvchi – Xoliq shunday qudrat egasiki, u olamni yaratishda kamon tortish
                        san`atini ishga soladi. Sodiq Feruz gʻazaliga yangicha mazmun bagʻishlaydi. Ya`ni Feruz Tangriga
                        umumiy ta`rif bersa, Sodiq uning har bir sifatini ochib berishga harakat qiladi. Ollohning
                        qudrati ila “osmon muallaq” tura oladi. “Yetti doʻzax va sakkiz jinonni zohir aylagan” iloh
                        shoir ta`biricha, barcha yerda hoziru nozir.
                        <br>
                        <br>Ammo Sa`diy gʻazallarini yuksak saviyada deb boʻlmaydi. U ayrim payrav gʻazallarida taqlid
                        doirasidan chiqa olmagan. Jumladan, “paydo” radifli gʻazali Feruz misralariga hamohang boʻlsa
                        ham, mazmun-mohiyati va obrazlar talqinida yangicha ifoda, originallik, oʻziga xoslik
                        koʻrinmaydi.
                        <br>
                        <br>Feruzda:
                        <br>
                        <br>Ne hikmatdurki, guldek yuz ato bir kimsaga aylab,
                        <br>
                        <br>Birovda bulbuloso ohu afgʻon beadad paydo.
                        <br>
                        <br>Sa`diyda:
                        <br>
                        <br>Birovni aylamish guldek yuzin husni malohatda,
                        <br>
                        <br>Birov qilmish oning ishqida afgʻon beadad paydo (B-258).
                        <br>
                        <br>Ollohning sifatlari cheksiz. Sa`diy Yaratganning sifatlariga murojaat qilar ekan, Feruz
                        gʻazalidagi ayrim soʻzlarning sinonimlarini qoʻllaydi. Quyidagi misralar fikrimizning dalilidir.
                        Jumladan, u Feruzning:
                        <br>
                        <br>Qachon hamdinggʻa goʻyo aylagum tilni mani bekas,
                        <br>
                        <br>Manga gar boʻlmasa altofi fazlingdin madad paydo,-
                        <br>
                        <br>baytiga quyidagicha hamohanglik qiladi:
                        <br>
                        <br>Nechuk bu bandai bechoralar hamdiga til ochmoq
                        <br>
                        <br>Madad gar qilmasa onga karamdin ul ahad paydo ( B-256).
                        <br>
                        <br>Ma`lumki, payrav, taqliddan, koʻchirmachilikdan iborat boʻlmay, shoirdan katta mahorat talab
                        qiladi.
                        <br>
                        <br>Oqil ijodida “paydo” radifli payrav gʻazal yangicha teran mazmun kasb etadi. Shoir Olloh
                        sifatlarini ta`riflashda bevosita Qur`on oyatlariga murojaat qiladi1:
                        <br>
                        <br>Sifatingdin biridur: “lam yalid”, biri “valam yulad”,
                        <br>
                        <br>Sanga kufr oʻlgʻusi kim desa: “mavlud abbu jadd*” paydo (B-476).
                        <br>
                        <br>Gʻazalni davom qildirib, Olloh jamiki jahon mulkiga malik ekanligini ta`kidlaydi. Uning
                        qudrati ila Sulaymon va Iskandar olamga hukmronlik qildi, tanlarga jon va nutq nasib etdi. Shoir
                        ta`biricha, Ollohning qudrati shu qadar sirliki, uni anglamoq mushkul. Zero, ilohiy sirlarni aql
                        bilan emas, balki koʻngil orqali his qilish mumkin.
                        <br>
                        <br>Bayoniy ijodida gʻazal tamomila oʻzgacha mazmun kasb etadi. Bu tabiiy. Zero, Bayoniy
                        shahzoda shoirlarning aksariyatiga ustozlik qilgan iste`dod sohibidir. Shoir Yaratganning
                        sifatlarini ta`riflashda uning ismlariga murojaat qiladi. “Toʻqqiz falakni paydo qilgan”
                        Parvardigorning ismi Rahimdir. Undan barcha umidvor boʻladi, rahmdilligi cheksiz. “Karimesanki,
                        in`oming bari olamga shomildur”, – deya fikrini davom qildiradi shoir. Gʻazalning keyingi
                        misralari yanada yetuk va ta`sirchandir:
                        <br>
                        <br>Iki mehmonsaro aylab bino sun`ingni me`mori,
                        <br>
                        <br>Boʻlubdur dahrda ahli qabulu ahli rad paydo.
                        <br>
                        <br>Birgina “paydo” radifli gʻazal Feruz davrida payravning goʻzal namunalari yaratilganiga
                        dalildir.
                        <br>
                        <br>Majmuada Ogahiy, Navoiy gʻazallariga bitilgan payravlar ham mavjud. Bu jihatdan Ogahiyning
                        “Mushkin qoshining hay`ati ul chashmi jallod ustina” matlali gʻazaliga bitilgan payravlar
                        mashhur va ibratlidir. Bu gʻazalga monand gʻazalni avvalo Feruz yozgan (sakkiz baytli). Soʻngra
                        shoh gʻazali asosida shahzoda shoirlarning payravga payravlari yaratildi. Muhimi shundaki, har
                        bir payravda Feruz gʻazalidagi mavzu, vazn va “ustina” radifi saqlangan holda turli qofiyalar
                        qoʻllangan. Bu oʻziga xosliklarni yorqin koʻrsatish maqsadida asos va payrav gʻazallarning
                        matla` va maqta`larini keltirish bilan kifoyalanamiz:
                        <br>
                        <br>Feruz:
                        <br>
                        <br>Payvasta qoshi shaklidur ul chashmi jodu ustina,
                        <br>
                        <br>Bir chargʻkim yozmish qanot qasd aylab ohu ustina.
                        <br>
                        <br>...Vah, naylayin Feruz, ul qotil gʻazab yoyin chekib,
                        <br>
                        <br>Mujgon oʻqin tez etgali chin solmish abroʻ ustina[16].
                        <br>
                        <br>Sa`diy:
                        <br>
                        <br>Tutmish qoshingni hay`ati ul yetishmas kon ustina,
                        <br>
                        <br>Yozdurgʻali moyil erur oʻq bu hazin jon ustina.
                        <br>
                        <br>...Sa`diy boʻlubon orazingni shavqida majnunshior,
                        <br>
                        <br>Yuz ranju mehnatlar bilan bu Nuhi xirmon ustina (B-264).
                        <br>
                        <br>Oqil:
                        <br>
                        <br>Hajri gʻami zoʻr aylamish bu zori noshod ustina,
                        <br>
                        <br>Qilmang ajab dod aylasam afgʻonu faryod ustina.
                        <br>
                        <br>...Furqat aro gar yigʻlasam ayb aylamagilki, Oqilo,
                        <br>
                        <br>Hajri gʻami zoʻr aylamish bu zori noshod ustina (B-479).
                        <br>
                        <br>Ta`kidlash kerakki, Oqil payravda Ogahiy gʻazalidagi qofiyalarni yoxud ularga ohangdosh
                        soʻzlarni qoʻllaydi (noshod, faryod, parizod, jallod, ozod) va matla`dagi birinchi misrani
                        maqta` oxirida takrorlab, ma`noni kuchaytiradi. Shuningdek, payravning ijodiy xarakteri Feruz
                        gʻazalidagi baytlarga emas, balki ularning muayyan qismiga bitishda ham koʻrinadi (Sa`diyda
                        toʻrt bayt, Oqilda besh bayt).
                        <br>
                        <br>Sultoniyning Feruzga payrav tarzida bitilgan boshqa bir gʻazalida ham ishq-muhabbat mavzusi
                        yetakchilik qiladi:
                        <br>
                        <br>Rahm aylabon bir nozanin keldi maning kulbam aro,
                        <br>
                        <br>Kulbam munavvar ayladi ochib uzori dilkusho (B-37).
                        <br>
                        <br>Ma`lumki, mumtoz adabiyotda “sallamno” radifli gʻazallar anchagina yaratilgan. Uning goʻzal
                        namunasini Xorazm adabiy muhitida Ogahiy she`riyatida uchratamiz. Ogahiyga payrav tarzida Feruz
                        ham “sallamno” radifli gʻazal yaratadi. Majmuadagi Sodiq va Sultoniy ijodidagi “sallamno”
                        radifli gʻazallar bevosita Feruz gʻazaliga payravdir. Sultoniyda:
                        <br>
                        <br>Dedim koʻrgan zamon xurshidi raxshoningga sallamno,
                        <br>
                        <br>Demasmu koʻrgan el bu la`li xandoningga sallamno (B-38).
                        <br>
                        <br>Shoir goʻzal yor timsolini hayotiy misralar orqali ta`riflaydi. Sodiq qalamiga mansb
                        misralarda esa oshiqlikning oʻzgacha talqini koʻzga tashlanadi:
                        <br>
                        <br>Koʻngul xoling havosi birla boʻldi band zulfingga,
                        <br>
                        <br>Bu yangligʻ dona birla bu sifat domingga sallamno (B-155).
                        <br>
                        <br>Sodiqning mahorati va iste`dodli shoir ekanligi Ogahiy gʻazaliga bitilgan quyidagi payravda
                        yaqqol koʻrinadi:
                        <br>
                        <br>Oldi koʻnglimni mani bir tiflvash jonongina,
                        <br>
                        <br>Qoshi yo, kipriklari oʻq, koʻzlari fattongina.
                        <br>
                        <br>Otashin ruxsorin ul oy ochsa nogoh bazm aro,
                        <br>
                        <br>Shavq oʻtigʻa oʻrtanib bagʻrim boʻlar biryongina (B-153).
                        <br>
                        <br>Shoir xalq qoʻshiqlariga monand oʻynoqi misralarda an`anaviy tashbehlar orqali ma`shuqaning
                        husni jamoliyu oshiqning holatini uygʻunlikda tasvirlaydi. Yuqoridagi faktlar shahzoda shoirlar
                        ijodining asosini tashkil etgan gʻazallar mumtoz an`analar doirasidagi ishqiy, orifona, ma`rifiy
                        mavzularda muxtare va payravlar shaklida bitilganini koʻrsatadi. Shu bilan birga, har bir
                        shoirning an`anaga ijodiy munosabati va oʻziga xosligi ham mavjud.

                    <p class="mbr-text mbr-fonts-style display-7">Toʻgʻri, bular miqdor va salmoq nuqtai nazaridan
                        majmuada katta mavqega ega emas. Lekin ular, nazarimizda, ikki jihatdan ahamiyatlidir:
                        birinchidan, bu janrlar shahzoda shoirlarning badiiy qiziqish doirasi kengligidan dalolat bersa,
                        ikkinchidan, ularning yaratilishi asriy adabiy an`ananing davomi sifatida shu davr adabiy
                        muhitida ijodkorlikning janriy rang-barangligi va gʻoyaviy-badiiy takomillashuvini koʻrsatishi
                        bilan diqqatga loyiqdir.
                        <br>
                        <br>Shuningdek, bu janrlarning mazmun-mohiyatiga aloqador yetakchi tipologik belgilar ham
                        majmuadagi namunalarda oʻz ifodasini topgan. Jumladan, qasidalarning mashhur tarixiy shaxslar
                        yoxud muhim voqealarga bagʻishlanishi, tarji`band, ruboiy va qit`alarda ijtimoiy-siyosiy va
                        ta`limiy qarashlarning, mustazod va masnaviylarda esa ishqiy-axloqiy motivlar ustuvorligi
                        saqlangan. Bulardan qasida ancha keng tarqalgan va faol janrdir. Mavzu jihatdan qasidaning ikki
                        turi – qasidai madhiya va qasidai bahoriya koʻp uchraydi. Qasidalardan biri Feruzni ulugʻlashga,
                        ikkinchisi esa Navroʻz ayyomi va bahor shodiyonasi voqealariga bagʻishlangan.
                        <br>
                        <br> Xorazm qasidachiligida ma`rifatli hukmdorlar va tabiat fasllarini tarannum etish an`anasi
                        ancha qadimiy boʻlib, “bu sohada Munis, Ogahiy, Komil Xorazmiy yaratgan qasidalar XX asr
                        boshidagi shoirlar uchun ham mahorat namunasi boʻldi”[1]. Shubhasiz, bular orasida shahzoda
                        shoirlar ham bor edi.
                        <br>
                        <br>Majmuadagi beshta qasida Sultoniy va Sodiq qalamiga mansub. Ularda Xorazm bilan bogʻliq
                        buyuk tarixiy siymolar va yirik voqealar madh etilib, janrning gʻoyaviy-badiiy arxitektonikasiga
                        toʻla amal qilingan. Bular orasida Sultoniy qasidalari alohida ajralib turadi. Uning uchta
                        qasidasida ham yuqorida qayd etilgan mavzular qalamga olingan. Ulardan biri bevosita bahor
                        ayyomiga bagʻishlanib, unda tabiatning betakror manzaralari tasviri Feruz haqidagi madhiya
                        talqinlari bilan qoʻshilib ketgan. Qasidaning dastlabki oʻn besh baytida bahorni ta`riflovchi
                        misralar keltirilib, tabiatning jonlanishi va olam goʻzalligi ifodalanadi:
                        <br>
                        <br>Bahor oʻldi ham mavsumi gul kelib,
                        <br>
                        <br>Guliston yuziga gul urdi xitob.
                        <br>
                        <br>… Kecha birla kunduz barobar oʻlub,
                        <br>
                        <br>Fuzun boʻldi xurshidgʻa nuri tob.
                        <br>
                        <br>… Jahon bogʻi gullar bila zeb topib,
                        <br>
                        <br>Qilur jannat ondin safo intisob (B-154).
                        <br>
                        <br>Bu baytlar qasidaning janriy talabiga koʻra nasib (kirish) boʻlib, unda tabiatdagi yasharish
                        va fusunkorlik tasvirlanadi hamda keyingi fikr rivojiga zamin hozirlaydi. Qasidaning
                        16-34-baytlari hukmdor (Feruz-I.X) madhiga bagʻishlangan asosiy qism gurizgoh, qolgan toʻrt bayt
                        esa xotima boʻlib, duodan iborat. Qasida shaklan oʻziga xoslikka ega. Uning dastlabki ikki bayti
                        lirik qahramonning dilbarga yuzidan niqobini olib, qorongʻu olamni munavvar qilish da`vati bilan
                        boshlanadi. Yor jamoli bilan bahor koʻrkamligining uygʻunligi qasidaning nasib qismida bayon
                        etiladi hamda ma`shuqa va bahor bir-birini toʻldirib, goʻzallik haqida yaxlit tushuncha beradi.
                        Chunonchi, Navroʻz ayyomida “hut dastidan chiqib, hamal gulshaniga oʻtgan yulduzlar”, oʻzaro
                        “tenglashgan kecha va kunduzlar”, “shabnam gulidan yuzga sepilgan gulobu”, bahor goʻzalligidan
                        “zeb topgan jannat bogʻi” talqinlari mantiqan ma`shuqa va bahorga bab-baravar tegishlidir.
                        Chunki bularning har ikkalasi ham goʻzallik timsolidir. Qasidaning madh qismida bahor
                        fusunkorligi shoh Feruzga xos fazilatlar bilan bogʻlanadi va gʻoyaviy uzviylik vujudga keladi.
                        <br>
                        <br>Shoir ta`rificha, “jahon shohlari Feruz xizmatidagi qullardir”. Albatta, bu oʻta mubolagʻa
                        boʻlib, shohni ulugʻlashga qaratilgan. Madhiyada boʻrttirish tabiiy hol boʻlsa ham, u muayyan
                        me`yorlarga – tarixiy haqiqatga muvofiqlikni taqozo etadi. Shu jihatdan “shahanshohi jahon”,
                        “shohi ofoq”, “sultoni olijanob” kabi sifatlar hamda afsonaviy va tarixiy buyuk siymolarga xos
                        xislatlar Feruzga koʻchiriladi. Ammo, uning ta`rifida tarixiy haqiqat ustuvorlik qiladi.
                        Quyidagi baytlarda bu fikrlar talmeh san`ati orqali ifodalanadi:
                        <br>
                        <br>Qilur qatl Bahrom oning dushmanin,
                        <br>
                        <br>Boʻlubon olargʻa shadid ul - uqob.
                        <br>
                        <br>… Agar adlini koʻrsa Noʻshiravon,
                        <br>
                        <br>Anga boʻlgʻusidur gʻulomi rikob.
                        <br>
                        <br>Zuhal xizmatin sidq ila aylabon,
                        <br>
                        <br>Taku poʻ qilur bilmayin xoʻrdu xob.
                        <br>
                        <br>Shoir ta`kidlashicha, bahor nafosati-yu Navroʻz shodligi adolatli shoh saxovati va marhamati
                        tufayli yanada fayzlidir. Bu qasidadan koʻzlangan asosiy gʻoyadir. Qasida yakunida shoir duo
                        hamda matlabining bayonini beradi, oʻz taxallusini keltiradi:
                        <br>
                        <br>Kelib har bahor ichra navroʻz ham,
                        <br>
                        <br>Qilur olam ahlini ishrat maob.
                        <br>
                        <br>Oning umri mehri topib irtifoʻ,
                        <br>
                        <br>Yetursin aqolimi olamgʻa tob.
                        <br>
                        <br>Shoirning ikkinchi qasidasida bu fikr yanada davom ettirilib, asosiy e`tibor Muhammad
                        Rahimxon madhiga qaratiladi. Bunda shohning odil, elparvar va raiyatparvarligiga oid talqin
                        birinchi oʻringa chiqadi. Shoirning ta`biricha, Feruz “soyai subhon” (Ollohning yerdagi
                        soyasi)dir. Uning odilligidan Xorazm jannatmakon yurtga aylangan:
                        <br>
                        <br>Lutf etibon Hazrati parvardigor,
                        <br>
                        <br>Keldi base fayz ila fasli bahor (B-152).
                        <br>
                        <br>Zero “Bergali ziynat ango ul soyai Subhon kelur”, “Adl ila Xorazm mulkin ayladi jannat
                        nishon” misralari fikrimizning dalilidir. Baytlardagi “fayz”, “bahor”, “adl”, “jannatnishon”
                        kabi tayanch soʻzlar orqali shoh fazilatlari talqiniga alohida urgʻu berilgan. Shoir qasidani
                        duo bilan yakunlaydi:
                        <br>
                        <br>Harnaki maqsud esa Haq berib,
                        <br>
                        <br>Qolmagʻay armoni oning zarravor.
                        <br>
                        <br>Sodiqning 23 baytdan iborat qasidasi gʻoyaviy yoʻnalishi va mavzu jihatdan Sultoniy
                        qasidasiga monand boʻlib, bahor va Feruz unda mamduh – maqtaluvchidir. Shu bois madhiya asosini
                        bahor vasfi va shoh ta`rifi tashkil etadi. Shoir peyzajdagi nafislik orqali ma`shuqa qiyofasini
                        toʻlaroq tasvirlashga intilsa, bahor shodligi vositasi bilan Feruz muruvvatpeshaligidan el
                        bahramandligi va quvonchiga ishora qiladi. Qasidada bahor faslining goʻzalligi, qushlarning
                        nolasiyu gullarning nozi, hovuzlardagi suvlarning yoqimli shildirashi Feruzdagi insoniy zohiriy
                        va botiniy fazilatlar, teran badiiy did bilan qoʻshilib ketadi:
                        <br>
                        <br>Bogʻlar ichra ochildi har taraf yuz rangi gul,
                        <br>
                        <br>Bulbuli bechoraning ishini qildi ohu zor.
                        <br>
                        <br>… Topti sahroi jahon ul nav` ziynat birla zeb,
                        <br>
                        <br>Kim sanga jannat gulistoni demakdin keldi or (B-422).
                        <br>
                        <br>Quyidagi baytda qasida Feruz topshirigʻiga binoan yozilgani alohida ta`kidlanadi:
                        <br>
                        <br>Bir qasida mashq qilgʻilkim, bori olam aro
                        <br>
                        <br>Aylasun tahsin anga jon birla ahli roʻzgor (B-423).
                        <br>
                        <br>Shoir zamondoshlari Feruzni adolatda Noʻshiravonga, saxovatda Hotam Toyiga tenglashtiradi,
                        Sodiq shohni ulardan ham yuqori qoʻyib, mubolagʻani qabartib qoʻllaydi:
                        <br>
                        <br>Adl rasmida anga Noʻshiravon ermas odil,
                        <br>
                        <br>Garchi bor erdi adolat ichra qilib sohib e`tibor.
                        <br>
                        <br>Yoki: Hotami Toyi boʻlur ehsonidin ummidvor.
                        <br>
                        <br>Biroq qasida oxirida Sodiq emas, Sultoniy taxallusi keltirilgan. Aslida manbada “Qasidai
                        Sodiq” deb sarlavha qoʻyilgan:
                        <br>
                        <br>Uyla oliyqadr Sultoniydururkim, yetkusi,
                        <br>
                        <br>Ostoni qulluqidin shohlargʻa iftixor.
                        <br>
                        <br>Bu oʻrinda Sultoniy qasidasi tasodifan Sodiqqa nisbat berilgan yoki taxallusda manbani
                        tuzuvchi xatoga yoʻl qoʻygan boʻlishi mumkin.
                        <br>
                        <br>Majmuada tarji`bandning ikki namunasi mavjud boʻlib, Sultoniy qalamiga mansubdir. Tarji`band
                        – mumtoz adabiyotdagi yirik lirik janrdir. U bir necha mustaqil bandlardan tarkib topadi. Har
                        bir bandning birinchi va soʻnggi baytlari masnaviy usulida qofiyalanadi. Banddagi toq misralar
                        ochiq qolib, juft misralar birinchi bayt misralari bilan qofiyadosh boʻlib keladi.
                        <br>
                        <br>Sultoniy tarji`bandlarida bu janriy xususiyatlar yorqin ifoda etiladi. Shoir tarji`bandning
                        har bir bandida soqiyga murojaat qiladi. Asarda muhabbat bahor ayyomining goʻzal damlariga
                        uygʻun holda tasvirlanadi. Shoir goʻzallikdan bahs qilar ekan, bahor jozibasi va bu ayyomda
                        gullarning tarovatini qalamga oladi, ularni ilohiylik tarovati, may bazmi gʻoyalari bilan
                        uygʻunlashtiradi:
                        <br>
                        <br>Ketur, soqiy, qadah fasli bahori bahjatafzodur,
                        <br>
                        <br>Yana ham mavsumi gul barcha gulshan jannatosodur.
                        <br>
                        <br>Bu gulshanda koʻngulni gʻunchadek tang qilmogʻil jonon,
                        <br>
                        <br>Koʻngul komicha barcha ishrat asbobi muhayyodur.
                        <br>
                        <br>Ochildi bogʻ aro gullar, bu mavsumni gʻanimat bil,
                        <br>
                        <br>Tutub gulrang jomin elga lola ishrat iymodur.
                        <br>
                        <br>Gʻami moziy-yu mustaqbalni chekma, ey hazin koʻnglim,
                        <br>
                        <br>Ajab bahjatfizo ayyomdur, jomi sahbodur.
                        <br>
                        <br>Koʻngul oynasidin zangi gʻamni yoʻq qilay desang,
                        <br>
                        <br>Kirib sayr ayla gulshan gullarini xush tamoshodur.
                        <br>
                        <br>Ochildi gʻuncha tumori, ma`lum oʻldi mazmuni,
                        <br>
                        <br>Budurkim favt qilmang, mavsumi gul jomi gulguni (B-148).
                        <br>
                        <br>Tasavvuf ta`limotiga koʻra, soqiy may quyuvchidir. May ma`shuqa chehrasida zuhurlangan
                        ilohiy nurni eslatuvchi Olloh ne`mati. May- vujudi mutlaq. Soqiy – esa uni oshiq vujudida
                        bot-bot jonlantiruvchi va oshiqni vujudi mutlaq ishqida oʻrtaguvchi vosita. Shu bois may oshiq
                        holatini aniqroq tasvirlashga xizmat qiladi. Bu tarji`bandning keyingi bandlarida oshiqning qalb
                        kechinmalari, botiniy tuygʻulari yanada kengroq tasvirlanadi. Baytga jom obrazini kiritish
                        orqali shoir oshiqning qalb hissiyoti va ehtiroslarini kengroq ochib beradi. Zero, jom ishq mayi
                        bilan toʻldirilgan oshiq qalbidir. Oshiq bu jomni ichgan bilan u ado boʻlmaydi, aksincha, toʻlib
                        boraveradi. Shoir bu falsafani misralar zamiriga singdiradi:
                        <br>
                        <br>Ketur, soqiy, qadah gulshan humoy dorul-qaror oʻldi,
                        <br>
                        <br>Ochildi rang-barang gul bogʻ ichra lolazor oʻldi.
                        <br>
                        <br>Chaman shohidlari shavqida faryodu figʻon aylab,
                        <br>
                        <br>Shajarlar ustida bulbul ishi afgʻonu zor oʻldi.
                        <br>
                        <br>Oqib suv bu nahrlar ichra har necha boʻlub mavvoj,
                        <br>
                        <br>Sarosar sabz oʻlub ashjor, hamchun xatti yor oʻldi.
                        <br>
                        <br>Mukaddar xotir oʻlma sahni gulshanda tuzub majlis,
                        <br>
                        <br>Guliston gullarini boʻyi chun mushki totor oʻldi.
                        <br>
                        <br>Gʻanimat anglabon fasli rabi`da royini zavqin,
                        <br>
                        <br>Sumurgil mavsumida la`lgun sharobkim, bahor oʻldi (B-149).
                        <br>
                        <br>Majmuadagi bir mustazodning muallifi ham Sultoniydir. XIV asrda Hofiz Xorazmiy boshlab
                        bergan va mumtoz she`riyatda keng qoʻllaniladigan mustazod an`anaviy janr sifatida XX asr
                        boshida kam boʻlsa-da, uchrab turadi. Uning oʻziga xos xususiyati shundaki, gʻazalga monand
                        yoʻlda yoziladigan she`rning har bir misrasi oxiriga aruz qoidasi asosida bir necha soʻz
                        orttiriladi. Bu soʻzlar shartli ravishda nim misra deyiladi.
                        <br>
                        <br>Mustazodda Sultoniy ishq-muhabbat mavzusini tarannum etadi. Oshiqning orzulari gʻam-alamlari
                        bilan uygʻunlashib ketadi. U jallod koʻzli ma`shuqadan madad kutadi, vasliga intiladi:
                        <br>
                        <br>Bogʻ ichra fido qomatinga, ey shahi xubon,
                        <br>
                        <br> shamshod ila ar-ar,
                        <br>
                        <br>Bazmimni yuzing mehridin et, ey guli xandon
                        <br>
                        <br> bir kecha munavvar.
                        <br>
                        <br>Hajring tunida qoʻyma mani vaslinga yetkur,
                        <br>
                        <br>Rahm aylabon, ey gul,
                        <br>
                        <br>Jallod koʻzing koʻrgan, ey ofati davron,
                        <br>
                        <br>der kofiri Xaybar.
                        <br>
                        <br>Shoir mustazodi 14 misradan iborat boʻlib, oxirida oʻz taxallusini keltiradi:
                        <br>
                        <br> Hajr oqshomining subhi gar bu kecha, ahbob,
                        <br>
                        <br> otsa ne ajabkim,
                        <br>
                        <br> Sultoniygʻa aylab karamin behaddi poyon,
                        <br>
                        <br> lutf ayladi dilbar.
                        <br>
                        <br>“Haft shuaro” majmuasida shahzoda shoirlarning kichik janrdagi she`rlari silsilasida ruboiy
                        ham muhim oʻrin egallaydi. Ular soni 18 ta boʻlib, Sultoniy (1), Farrux(1), Oqil (5), Murodiy
                        (1) va Bayoniy (10) tomonidan yaratilgan.
                        <br>
                        <br>Ruboiy mumtoz adabiyotimizda toʻrt misradan iborat ijtimoiy-falsafiy fikr ifodalovchi
                        mustaqil janr boʻlib, XX asr boshida ham she`riyatimizda muayyan nufuzga ega edi. Zero, “Ruboiy
                        toʻrt misraga sigʻdirilgan butun bir she`riyat dunyosi, insonning gʻoyat xilma-xil va juda boy
                        fikr-hislari, “qalb dialektikasi”ning ajoyib tarjimonidir”[2].
                        <br>
                        <br>Mumtoz she`riyatimiz takomilining barcha bosqichlarida “Xorazm shoirlari hajman kichik
                        boʻlsa-da, ammo katta masalalarni ifodalash xarakteriga ega boʻlgan ruboiydan keng va unumli
                        foydalandilar”[3].
                        <br>
                        <br>Adabiyotshunos E.Ochilov ruboiy janrining tarixiy taraqqiyotini tadqiq qilar ekan, XVII-XIX
                        asrlarda janrni shakl va mazmun jihatdan yangilashga intilish, XX asrda esa mumtoz
                        ruboiynavislik an`analarini yangicha sharoitda davom etishi xarakterli ekanini ta`kidlaydi[4].
                        <br>
                        <br>Darhaqiqat, XIX asr oxiri va XX asrning boshi Xorazm adabiy muhiti ruboiynavisligida ham bu
                        ikki xususiyat mavjud boʻlib, shahzoda shoirlar ijodida uning namunalarini koʻramiz. Bularning
                        bir qismi shahzoda shoirlarga tegishli boʻlib, ular bisotida qofiyalanishiga koʻra har ikkala
                        turi – xosa (a,a,b,a) va taronai ruboiy (a,a,a,a) ham uchraydi va aruzning hazaj bahridagi axrab
                        va axram vaznlarida yozilishiga qat`iy amal qilingan. Shuni ta`kidlash kerakki, bu ruboiylarda
                        janr uchun ustuvor boʻlgan falsafiy mavzu bilan birga ishqiy talqinlarga ham duch kelamiz.
                        Chunonchi, Sultoniy, Murodiy ruboiyotida muhabbat mavzusi, Farrux, Oqil va Bayoniy ruboiylarida
                        falsafiy-ta`limiy fikrlar yetakchilik qiladi. Shunga monand ravishda turli badiiy-tasviriy
                        vositalar va uslub qoʻllaniladi. Jumladan, ishqiy-intim mavzularda koʻproq ob`ekt ta`rifi hamda
                        kechinmalar jozibali ifodalansa, falsafiy-axloqiy ruhdagi ruboiylarda tamsiliy hamda
                        ijtimoiy-didaktik umumlashmalar keltiriladi. Sultoniy ruboiysi yorga murojaat bilan boshlanadi.
                        Ma`shuqadan koʻp jabru sitam koʻrgan oshiq alamdan eziladi va mahbubadan shafqat va arzu dodiga
                        yetishni soʻraydi. Toʻrt misraning har birida ushbu gʻamgin kayfiyat va umidvorlikni ifoda
                        etuvchi ma`nodosh soʻzlarning juft holda qoʻllanilishi ma`noni yanada chuqurlashtirgan
                        (jabr-sitam, karam-lutf, ranj-alam, qaygʻu-gʻam ) :
                        <br>
                        <br>Ey yor, manga aylama koʻb javru sitam,
                        <br>
                        <br>Hajr ichra manga yetti base ranju alam.
                        <br>
                        <br>Aylab karamu lutf, maning dodima yet,
                        <br>
                        <br>Ozurda qilibdur mani qaygʻu ila gʻam (B-150).
                        <br>
                        <br>Majmuadagi Oqilning beshta ruboiysidan ikkitasi ishqiy mavzuda, qolganlari falsafiy mavzuda
                        yozilgan boʻlib, uchtasi taronai ruboiydir. Quyidagi toʻrtlik taronai ruboiy namunasidir:
                        <br>
                        <br>Hay-hay ne balo qarodurur qosh sanga,
                        <br>
                        <br>Ishqim ne uchun boʻlmas ekan fosh sanga.
                        <br>
                        <br>Rahm aylaki, sadqa aylaram bosh sanga,
                        <br>
                        <br>Tangrim yana bersun iki yuz yosh sanga (B-532).
                        <br>
                        <br>Ruboiyning dastlabki misrasida lirik qahramonning hayrati yor qoshining qaroligidan
                        anglashiladi. Ikkinchi misrada esa fikr rivojlantirilib, oshiq oʻz sevgisini izhor qiladi.
                        Uchinchi misra ruboiy qoidasiga koʻra, koʻprik vazifasini oʻtaydi. Ma`shuqadan iltifot kutgan
                        oshiq uning rahm qilishi uchun oʻz boshini sadaqa qilishga tayyordir. Oxirgi misrada esa
                        an`anaviy tarzda Ollohdan ma`shuqaga umr tilaydi. Shu misra orqali “ishqi fosh” boʻlmagan
                        ma`shuqadan rahm kutgan oshiqning fidoiyligi gʻoyasi ruboiy zamiriga singdirilgan. Ruboiy uslub
                        jihatdan ravon va sodda boʻlib, oshiq kechinmalarini ta`sirchan ohangda aks ettiradi. Toʻrt
                        misra oxirida “sanga” radifining bevosita ma`shuqaga qaratilishi va “qosh”, “fosh”, “bosh”,
                        “yosh” toʻliq qofiyalarining mahorat bilan qoʻllanilishi poetik mukammallikni oshirgan.
                        <br>
                        <br>Oqilning keyingi ruboiysi ham ishqiy mavzuda boʻlib, mahbubaning goʻzalligi
                        detallashtirilgan holda tasvirlanadi. Oshiqning ta`biricha, yorning yangi chiqqan moʻylabi
                        (xatti) husniga husn qoʻshgan, shu tufayli u shu qadar goʻzalki, hatto unga husn ahli boj
                        toʻlasa arziydi. Uni goʻzallik mamlakatining taxtiga hukmron qilib, boshiga toj kiydirsa ham
                        kam. Zero, mahbuba vasliga shoh ham, gado ham birday muntazir:
                        <br>
                        <br>Bermishdurur husninga xating asru rivoj,
                        <br>
                        <br>Vaslingga gadolaru gʻaniylar muhtoj.
                        <br>
                        <br>Loyiq sanga husn ahlidin olmoq boj,
                        <br>
                        <br>Ham mulki malohatda sanga taxt ila toj (B-532).
                        <br>
                        <br>Oqilning uchta ruboiysidagi jamiyat, xalq va hayot haqidagi qarashlar hukmdor (Feruz –I. X)
                        madhi fonida bayon etiladi. Bu hol tasodifiy emas. Ma`lumki, shahzoda shoirlar, jumladan, Oqil
                        ham taqvodor boʻlib, inson kamolotini islomiy e`tiqod va Olloh inoyatidan deb bilgan va targʻib
                        qilgan. Shu tufayli Feruz siymosi ham bu kamolotning yuksak namunasi sifatida tarannum etilib,
                        undagi barcha fazilatlar ilohiylik tajallisi bilan bogʻliq holda talqin qilinadi. Buni quyidagi
                        ruboiyi xosada ham koʻramiz:
                        <br>
                        <br>Yo rab, shohim umru davlatin mavfur et,
                        <br>
                        <br>Olam eli xalqin amriga ma`mur et.
                        <br>
                        <br>Tab`igʻa muxammasimni maqbul aylab,
                        <br>
                        <br>Doim karami birla mani masrur et (B-531).
                        <br>
                        <br>Ruboiyda falsafiy izchillik mavjud. Shoir avval Ollohdan shohga koʻp umr va davlatiga
                        farovonlik (mavfur) tilaydi. Chunki el ma`murligi shularga bogʻliq. Shoir esa xalqning bir
                        vakili sifatida oʻz ruboiysining shohga ma`qul boʻlishini istaydi. Zero, shoh karami shoir
                        masrurligidir. Ruboiydagi rab, shoh, davlat, xalq, shoir soʻzlarining mantiqiy bogʻliqligi
                        yaxlit ma`noni yorqin aks ettira olgan.
                        <br>
                        <br>“Haft shuaro” majmuasidagi kichik janrlardan biri muammodir. Turkiy adabiyotda Navoiy
                        tomonidan boshlab berilgan va XV asrdan keyin mumtoz she`riyatda muayyan oʻrin tutgan muammoning
                        XIX asrda birgina Ogahiy merosida 80 ta namunasi uchraydi. Bu an`anani Sultoniy davom ettiradi.
                        <br>
                        <br>Hajm jihatdan ixcham shakldagi bu janr chuqur falsafiy-axloqiy masalalarni bayon etishga
                        imkon beradi. “Ma`nosi berkitilgan soʻz” mazmunini ifodalovchi muammo bir baytda u yoki bu ismni
                        yashirib, uni topish uchun turlicha imo-ishoralarni bayon qilishni nazarda tutadi. Bunda arab
                        alifbosidagi harflarning shakli, nom xususiyatlari va abjad hisobiga murojaat etiladi.
                        <br>
                        <br>Manbada Sultoniyning uchta muammosi berilgan boʻlib, ularda teran mazmun mujassamdir. Shoir
                        bu janr sirlarini ustozi Ogahiydan oʻrganadi, oʻz navbatida ustoz shoirga ergashib, muammo
                        yaratadi. Jumladan, “Olloh” nomli muammoga nazar solaylik. Ogahiyda:
                        <br>
                        <br>Noz ila bugun ul shoʻx bogʻ sayrigʻa yurdi,
                        <br>
                        <br>Loladin birni olib boshi uzra qistirdi.
                        <br>
                        <br>Sultoniyda:
                        <br>
                        <br>Yorim ismin soʻrsang, ey ozurda jon,
                        <br>
                        <br>Loladin birni olib, qoʻy boshigʻa boʻlgʻay ayon (B-137).
                        <br>
                        <br>Muammoning yechimi ikkinchi misrada. Abjad hisobiga koʻra, “alif” harfi “bir” raqamini
                        anglatadi, arab alifbosida yozilgan “lola” soʻzidan bir, ya`ni “alif”ni olib, “lola” soʻzining
                        boshiga qoʻysak, “Olloh” soʻzi kelib chiqadi va muammo yechiladi.
                        <br>
                        <br>Muammo janriga mansub yuqoridagi baytlar shakliy jihatdan oʻxshash boʻlsa-da, mazmunan
                        bir-biridan farqlanadi. Ya`ni Ogahiy baytda Olloh goʻzallikning yaratuvchisi ekanligiga ishora
                        qilsa, Sultoniy uni ma`shuqa sifatida tasvirlaydi. Bu hol quyidagicha ifoda topadi: birinchi
                        muammoda “shoʻx goʻzal” birinchi oʻringa oʻtadi va bogʻ sayliga yurib, “loladin birni olib
                        boshiga qistirish ” bilan muammo yechimini topishda faollik koʻrsatsa, ikkinchi muammoda oshiq
                        mahbubasining ismini soʻrovchi “ozurda jon”ga ismni toʻgʻridan-toʻgʻri aytib qoʻya qolmay, zarda
                        ohangda “Loladin birni olib qoʻy boshigʻa boʻlgʻay ayon”,– deydi[5].
                        <br>
                        <br>“Muhammad” nomli muammo ham Ogahiy va Sultoniy ijodida deyarli oʻxshashlik kasb etadi.
                        Ogahiyda:
                        <br>
                        <br>Gʻamdin tushti koʻngul uzra iydsiz afvoj,
                        <br>
                        <br>Ey yor, sendin oʻlmish hadsiz madadga muhtoj.
                        <br>
                        <br>Sultoniyda:
                        <br>
                        <br>Boshimgʻa keldi gʻamdin hajringda behad afvoj,
                        <br>
                        <br>Ey shoʻx, sandin oʻlmish hadsiz madadga muhtoj (B-137).
                        <br>
                        <br>Bu muammo ikkala misraning soʻz ishoralari asosida yechiladi. Intiqod amaliga koʻra,
                        “gʻamdin” soʻzidagi “mim” va “dol” harflarini saralab olamiz. Ikkinchi misradagi “muhtoj”
                        soʻzining ikkinchi boʻgʻinini “hadsiz” qilib olib tashlaymiz. Qolgan harflar va yuqoridagi
                        harflarni ta`lif qoidasi asosida oʻrni-oʻrniga qoʻyamiz. Natijada, “Muhammad” soʻzi paydo
                        boʻladi.
                        <br>
                        <br>Shoirning “Muhammad Rahim” nomli muammosida muammo san`atining yangi qoidasi ishga solinadi:
                        <br>
                        <br>Ta`mil aylab yoshurdi ismini bu bayt aro,
                        <br>
                        <br>Ismi haqgʻa Mustafo ismini qoʻsh boʻlgʻay ayon (B-138).
                        <br>
                        <br>Muammoning yechimi ikkinchi misrada tarodif qoidasiga koʻra, “Haq” soʻzining sinonimlaridan
                        biri “Rahim” soʻzi tanlab olinadi. “Mustafo” soʻzining sinonimi “Muhammad” soʻzi olinib, tarodif
                        qoidasi asosida birlashtiriladi va “Muhammad Rahim” soʻzi kelib chiqadi. Bu muammo oʻz-oʻzidan
                        ma`lumki, Feruzga bagʻishlab yozilgan. Shuningdek adabiyotshunos
                        <br>
                        <br>J. Joʻrayev Feruzning kichik inisi Sayid Homid toʻra Komyob qalamiga 12 ta muammo mansub
                        boʻlib, ularda “latif ishqiy kechinmalar va pand-nasihat ohanglari ” yetakchi oʻrinda turishini
                        ta`kidlaydi[6].
                        <br>
                        <br>Majmuadan oʻrin olgan kichik lirik janrlardan yana biri qit`adir. Adabiyotshunos I. Haqqulov
                        yozadi: “U (qit`a–I.X) poetikaning “temir qonunlari” iskanjasida ozod ruhda hayot kechirgan...
                        shuning uchun soʻzda, obrazda, shaklda ortiqcha mumtozlikni, boʻyoqdorlikni talab qilmagan va bu
                        janrda realistik ohanglar, hayotiy detallar lirikadagi boshqa janrlarga qaraganda keng va koʻp
                        boʻlgan”[7]. Bayoniy qalamiga mansub qit`alarda ham bu xususiyatlar mavjud boʻlib, ular
                        an`anaviylikka ijodiy yondoshuv namunasidir. Chunonchi, Xorazm adabiy muhitida nisbatan kam
                        uchraydigan qit`achilikda XIX asrdan boshlab Ogahiy, soʻngra Komil, Avaz, Roqim ijodida
                        “Faloniy” nomi bilan turkumlangan qit`alar maydonga kelib, ularda falak zulmi va zamondan, dahr
                        elidan norozilik kayfiyatlari ifodalansa, Bayoniy qit`alarida ijtimoiy va lirik motivlar
                        uygʻunlikka, ma`rifat va insoniy komillik talqiniga asosiy diqqat qaratildi. Shoir ikki yoki
                        undan ortiq juft misralari oʻzaro qofiyalangan baytlardan iborat boʻlib, mavzu va vazn doirasi
                        keng qamrovli qit`a imkoniyatlaridan mahorat bilan foydalandi. Janrning “mustaqil asar va qasida
                        hamda gʻazal janrining tarkibidagi bir parcha”[8] sifatida shakllanishiga e`tibor berib,
                        birinchi usulni – mustaqil qit`a yaratishni tanladi.
                        <br>
                        <br>Bayoniy qit`alari besh va olti baytdan iborat. Gʻazaldagi qofiyadosh matla` ularda
                        qofiyalanmaydi, keyingi baytlar b-a; v-a; g-a; shaklida qofiyadoshdir. Quyidagi bayt shunga
                        misoldir:
                        <br>
                        <br>Boʻstoni mahfilimda bir gul ochildi goʻyo,
                        <br>
                        <br>Gulshanda ming chaman ham ermas anga barobar.
                        <br>
                        <br>Ma`shuqa ta`rifiga bagʻishlangan ishqiy mavzudagi bu qit`ada yorning goʻzal va maftunkor
                        qiyofasi gavdalantiriladi. Ikkinchi baytda matla` oxiridagi “goʻyo” soʻziga “yakto”, juft misra
                        oxiridagi “barobar” soʻziga “ahmar” soʻzi ohangdoshlik qiladi. Bayt mazmunida ma`shuqa “latofat
                        jahonida yagona” boʻlib, u tengsiz “vardi ahmar” (qizil gul)ga tenglashtiriladi. Keyingi baytda
                        yor nafosati Chin (Xitoy) mushki hushboʻyligidan ham yuksak, jamoli oy va quyosh (nur)dir deya
                        ta`riflanadi. Yakunlovchi beshinchi baytda husni latofatda yakto ma`shuqaning oshiq qalbida
                        toʻpolon qoʻzgʻab junbushga keltirgan va hayron etgan iztirob bayon etiladi:
                        <br>
                        <br>Ul yorning gʻamidin muztar boʻlib Bayoniy,
                        <br>
                        <br>Tushmish boshiga har dam yuz nav` shoʻri mahshar.
                        <br>
                        <br>Odatda, qit`ada taxallus qoʻllash shart boʻlmasa ham, Bayoniy qit`alarida taxallus mavjud.
                        Shu bois ayrim manbalarda, “etti baytdan ortiq boʻlgan qit`alarda shoir taxallusi ham kelishi
                        mumkin” degan fikr[9] qat`iy qoida boʻlmay, bu hol yettidan kam baytli qit`alarga ham
                        taalluqlidir (xususan qit`a-gʻazallarga ).
                        <br>
                        <br>Bayoniyning yana bir qit`asi olti baytli boʻlib, kotiblik san`atiga bagʻishlangan. U ham
                        qofiyasiz matla` bilan boshlanadi va juft misralari ikki hijoli toʻliq qofiyadosh “raqam”,
                        “hadam”, “qalam”, “adam”, “alam” va “karam” kabi ot turkumiga mansub soʻzlar bilan yakunlanadi.
                        Qit`ada kotiblik badiiy asar targʻibotida kitobat va kotibning burchi, “soʻz binosini durust
                        etish”dagi oʻrni, yaxshi kotiblar hunari e`zozga, yomonlari esa jazoga loyiqligi xususida fikr
                        yuritiladi. Shoir xat bituvchi, raqam chekuvchi badkor kotibning “ikki yuzi Tangri siyohidin
                        qaro” ekanini ta`kidlar ekan, bu xayrli ishda ham oʻzini Olloh karamidan umidvorligini izhor
                        qiladi. Bayoniy adabiy-estetik qarashlarining bir qirrasini bevosita va yorqin ifodalagan hamda
                        koʻpchilikka noma`lum boʻlgan ushbu qit`a matnini quyida keltiramiz:
                        <br>
                        <br> Har kotibi xudroykim, badkorliq rasmin tutub,
                        <br>
                        <br> Alfozning mahfiga yo isbotiga chekmish raqam .
                        <br>
                        <br> Bilmoq qavoidi kalom uzra suxanronliq fanin,
                        <br>
                        <br> Soʻzning binosini durust etdim debon qilsa hadam.
                        <br>
                        <br> Tangri siyohidin iki ruxsorini aylab qaro,
                        <br>
                        <br> Qilsun qazo tigʻi bila ilkin qalam yongligʻ qalam.
                        <br>
                        <br> Barcha xavosin harfin boʻyi vujudidin qarib,
                        <br>
                        <br> Qilsun jahonning lavhidin harfi vujudini adam.
                        <br>
                        <br> Andogʻ yamon kirdorigʻa mundogʻ jazolarni topib,
                        <br>
                        <br> Soʻngra yamon oti qolib olam aro boʻlsun alam.
                        <br>
                        <br> Shundogʻ oʻlub bebahrai mufsidga bu yangligʻ duo,
                        <br>
                        <br> Aylar Bayoniy, mustajob etgay xudo aylab karam.
                        <br>
                        <br>Shuningdek, majmuada Sultoniy, Sa`diy, Oqil va Bayoniy yaratgan oʻnta masnaviy namunalari
                        ham bor. Ular mavzu va gʻoyaviy yoʻnalishi jihatidan asosan an`anaviy ta`limiy-ijtimoiy ruhda
                        boʻlib, mualliflarning olam va odam, hayot va oʻlim haqidagi qarashlarini aks ettiradi. Bular
                        orasida, xususan, Sultoniy va Bayoniy masnaviylari falsafiy mazmunning chuqurligi bilan ajralib
                        turadi. Bu oʻrinda Sultoniyning insonning real zamindagi oʻrni, umrning mohiyati, nafs va
                        hashamga munosabati, umuman, dunyoning foniyligi toʻgʻrisidagi masnaviysidan parcha keltirish
                        bilan cheklanamiz. Unda shoir oʻzining muxtasar va ibratli fikrini ta`sirchan va obrazli tarzda
                        ifodalay olgan:
                        <br>
                        <br> Har kishi kelsa jahon boʻstonigʻa,
                        <br>
                        <br> Tez tushar, chiqmas oning zindonigʻa.
                        <br>
                        <br> Yuz uqubat birla davlatgʻa yetar,
                        <br>
                        <br> Mulki molu hashmatin tashlab ketar (B-138).
                        <br>
                        <br>Umuman, majmuadagi musammat shakllaridan boshqa janrlardagi she`rlarda har bir muallifning
                        oʻziga xos fikrlash tarzi, poetik ifoda usuli, an`anaga ijodiy munosabati mavjud boʻlib, ular
                        kengroq tadqiqot ob`ekti sifatida oʻrganishga loyiqdir.

                    <p class="mbr-text mbr-fonts-style display-7">Bular jamlangan manbalar – “Haft shuaro” majmuasi va
                        Tabibiyning “Majmuai si shuaroi payravi Feruzshohiy” va “Muxammasoti majmuat ush- shuaroi
                        Feruzshohiy” tazkiralari Feruz tashabbusi bilan yaratildi. Ularning birinchisi 1906-yilda,
                        ikkinchisi 1908-yilda, uchinchisi 1909-yilda tuzildi. Kuzatishlarimizcha, qisqa vaqtda vujudga
                        kelgan bu adabiy manbalarni oʻzaro bogʻlovchi ichki mantiqiy aloqadorlik mavjud. Jumladan, ular
                        oʻsha davr shoirlarining lirik she`rlarini toʻplam holida jamlashga qaratilgani tufayli uchalasi
                        ham umumiy nom bilan “majmua” deb atalgan. Ikkinchidan, manbalarning asosi Feruz va uning
                        shajarasi ijodkorlari asarlaridan iborat boʻlib, u mustaqil va payrav she`rlar shakllarida
                        ifodalangan. Ya`ni “Haft shuaro”da turli janrdagi she`rlar mujassam boʻlsa, tazkiralarda
                        she`rlar bilan birga, biografik va adabiy ma`lumotlar ham berilgan. Uchinchidan, “Haft
                        shuaro”dagi koʻpchilik shoir va she`rlar tazkiralardan ham oʻrin olgan boʻlib, ular mualliflar
                        shaxsiyati va ijodiga oid Tabibiy bitgan masnaviy-ma`lumotlar bilan toʻldirilgan. Shu ma`noda
                        tazkiralarni “Haft shuaro”ning ma`lum darajada takomillashtirilgan davomi ham deyish mumkin.
                        Binobarin, bu ma`lumot-masnaviylar “Haft shuaro”dagi shoir va she`rlar haqidagi ma`lumotlarni
                        yangi fikrlar bilan boyitishga, ularni kengroq tasavvur etishga xizmat qiladi. Shu bois
                        tazkiralardagi adabiy va biografik ma`lumotlarga “Haft shuaro”ni oʻrganishda bevosita aloqador
                        boʻlgan muhim materiallar sifatida qarab, ulardan ham foydalanish maqsadga muvofiqdir. Chunki
                        shoirlar shaxsiyatiga oid tafsilotlar majmuadagi she`rlarni chuqurroq tasavvur qilishga va
                        ijtimoiy hamda hasbi hol motivlari uygʻunligini tadqiq etishga yordam beradi.
                        <br>
                        <br>Ta`kidlash kerakki, “Haft shuaro”ga she`rlari kirgan shahzoda shoirlar haqidagi ma`lumotlar
                        keyingi davrlarda vujudga kelgan Laffasiy va Xodim tazkiralaridan ham keng oʻrin olgan.
                        Bularning birinchisida Tabibiy tazkiralaridagiday har bir shoir xususidagi ma`lumot va
                        she`rlardan namunalar beriladi, ikkinchisida esa faqat shaxsiyatiga doir ma`lumotlar keltirilib,
                        ijodlari umumiy tarzda ta`riflanadi. Shu sababli Xodim tazkirasidagi talqinlarni shoirlarning
                        oʻziga xos adabiy portretlari yoki portretga chizgilar deyish mumkin. “Haft shuaro” va
                        tazkiralarning qisqa muddatda yaratilishi hamda oʻzaro bogʻliqligiga shaxsan Feruz rahnamoligi
                        asosiy omil boʻlgani, shubhasiz. Shu bilan birga, nazarimizda, shohning qiziqishi va manfaatlari
                        hal qiluvchi rol oʻynagan boʻlsa-da, quyidagi jihatlarni ham qayd etish kerak:
                        <br>
                        <br>1. Feruzning tabiatan adabiyotga va san`atga cheksiz ishtiyoqi, mehri hamda yuksak badiiy
                        did sohibi ekanligi;
                        <br>
                        <br>2. Aksariyat xukmdorlar qatori badiiy ijod hamda uning targʻiboti orqali oʻz xukmronligi va
                        mavqeini mafkuraviy ta`minlashga erishish;
                        <br>
                        <br>3. Shoir va san`at muhibi sifatida oʻzidan ezgu nom qoldirish va mangulikka daxldorlik
                        tuygʻusi;
                        <br>
                        <br>4. Rossiyaga qaramlik sharoitida ma`naviy-ma`rifiy taraqqiyotga intilish va akademik Oybek
                        ta`kidlaganidek, Feruzning faqat xalq uchun boʻlmasa-da, oʻz saroyi uchun madaniyat tusi
                        berishga majbur boʻlishi.
                        <br>
                        <br>“Haft shuaro” majmuasi ana shu omillarning hosilasi va Feruzning avvalo, oʻz muhitiga
                        “madaniyat tusi” berishga intilishining dalilidir. Ikkinchidan, tazkiraga faqat shohning doʻstu
                        qarindoshlarining asarlari tanlab olinganida shaxsiy mayl ustuvor boʻlib, Feruzning oʻz
                        yaqinlariga gʻamxoʻrligi va ularning badiiy iqtidoriga e`tibori kuchliligini koʻrsatish
                        zaruriyati ham majmuani yaratishga turtki boʻlgan. Lekin xolis hamda qattiqqoʻl shoh bu ishga
                        jiddiy talab va mas`uliyat bilan yondashgan koʻrinadi. Chunki oʻz avlodi shoirlarining
                        koʻpchiligi ancha tanilgan va devonlar tuzgan boʻlishsa ham, ulardan faqat yetti nafarining
                        she`rlari majmua uchun tanlangan. Hatto unda Feruzning ancha salmoqdor ijodkor inisi Komyob,
                        shuningdek jiyanlari Gʻoziy va Asadlarning asarlari ham uchramaydi. Nisbatan kamroq she`rlar
                        yaratgan inisi Murodiy va oʻgʻli Farrux (Asfandiyor) asarlari esa majmuaning dastlabki
                        sahifalarini tashkil etadi. Demak, shoh majmuani tuzishni niyat qilganda ijodkor qarindoshlarni
                        va she`riy asarlarni tanlashda muayyan maqsadlarni hisobga olgani, shubhasiz. Ammo majmuada
                        faqat yetti nafar shoir she`rlari kiritilgani sababi haqida ma`lumot berilmaydi. Nazarimizda,
                        bunda birinchidan, bevosita Bayoniy ustozlik qilgan va tavsiya etgan, ikkinchidan, she`rlari va
                        shaxsiyati Feruzga ma`qul boʻlgan shahzodalar tanlangan koʻrinadi. Majmua debochasining shaxsan
                        Bayoniy tomonidan yozilgani ham shunday fikrga asos beradi. Uchinchidan, majmuani tuzish va
                        debocha yozishning Bayoniyga topshirilishi ham tasodifiy boʻlmay, bunda uning ustozlik maqomi,
                        yuksak tashkilotchilik nufuzi va ilmiy-badiiy mahorati nazarda tutilib, uning maslahati ham
                        inobatga olingan boʻlishi mumkin. Binobarin, Bayoniy Feruzning nisbatan uzoqroq qarindoshi
                        boʻlsa-da (Eltuzarxonning nabirasi), uning she`rlariga katta oʻrin berilishi ham shunday fikr
                        tugʻdiradi.
                        <br>
                        <br>Debocha shakli va mazmuni majmuaning ayrim oʻziga xos jihatlarini kuzatishga imkon beradi.
                        Avvalo, debocha an`anaviy usuldan farqli ravishda asarning boshida emas, Sultoniy she`rlari
                        oldida berilgan boʻlib, uch sahifadan iborat. Mazmuniga koʻra, u ikki qismdan tashkil topgan:
                        birinchi qism Feruzni madh etishga, ikkinchi qismi esa Sultoniy ijodi ta`rifiga bagʻishlangan.
                        Nasriy bayondan tashqari, unda 96 she`riy misra boʻlib, uning 42 tasi Feruz, 54 tasi Sultoniy
                        haqidadir. Sultoniy ijodiga Feruz va Bayoniyning bunchalik katta e`tibori tasodifiy emas,
                        nazarimizda. Bunda Sultoniy Feruzga eng yaqin va ardoqli nevara boʻlgani, unga Feruz ham,
                        Bayoniy ham ustozlik qilgani, Sultoniy she`rlari ma`qul va salmoqdor boʻlib, ularni devon
                        qilishni bobosi maslahat bergani kabi omillar hal qiluvchi rol oʻynagan koʻrinadi. Shu tufayli,
                        bizningcha, “Haft shuaro” dastlab Sultoniy asarlari asosida shakllanib, keyinchalik unga Feruz
                        tavsiyasi bilan boshqa shahzodalar she`rlari ham qoʻshilganini taxmin qilish mumkin. Debochaning
                        faqat Sultoniy ijodiga oid ekani xususidagi Bayoniy soʻzlari bu fikrimizga dalildir: “ishorati
                        oliy sudur toptikim, ul ash`or va gʻazallarni jam` qilib, debocha yosab murattab etgayman”.
                        <br>
                        <br>Debocha “Bismillahir rahmanir rahiym. Hamd ul suxanoro oligʻakim, qudrat komilasi bila soʻz
                        poyasin baland etib, ma`ni ahlin xiradpayvandlarga dilpisand etdi va shuaroi fasohatbayonning
                        guliston
                        <br>
                        <br>tab`larin balogʻat joʻyboridin sarshor etib...” kabi hamdu sano soʻzlari bilan boщlanib,
                        Feruzning shaxs va adolatli hukmdor sifatidagi fazilatlari ulugʻlanadi.
                        <br>
                        <br>Majmuadan, shuningdek, Feruzning nevaralari Sultoniy, Sa`diy, jiyanlari Sodiq va Oqil
                        she`rlari ham joy olgan. Darhaqiqat, ilmda e`tirof etilganidek, “Feruz shajarasiga daxldor
                        adabiy muhitda bularning (shahzoda shoirlarning – I.X) har biri oʻziga xos oʻrin va mavqega ega
                        edi. Ularning yaxshigina she`rlar majmuasi oʻz davrida “Xiva shoir shahzodalarining kitobi”,
                        “Haft shuaro” nomlari bilan xalq orasida tarqalgan. Bu adib va asarlar ham bizning
                        milliy-madaniy boyligimiz”[1].
                        <br>
                        <br>“Haft shuaro” majmuasining ikkita qoʻlyozma va oltita toshbosma nusxasi O`zR FA ShI
                        qoʻlyozmalar xazinasida saqlanmoqda.
                        <br>
                        <br>39 va 40-raqamli nusxalar Qoʻqon qogʻoziga nasta`liq xatida bitilgan. Hajmi 560 sahifa.
                        Manbaning har bir sahifasida 18-19 yozuv yoʻli bor. Nusxalar toʻliq emas. Ikkita nusxa deyarli
                        bir xil. Manbaning Murodiy ijodiga bagʻishlangan toʻrtta varagʻi yoʻqolgan. Majmua beshinchi
                        varaqdan boshlanadi. Unda Murodiyning oʻnta gʻazali mavjud. Manba Feruz saroyidagi tipografiyada
                        bosilgan. She`rlar ikki ustun shaklida berilgan. Sahifalar raqamlangan. 39-raqamli nusxa och
                        yashil rangli, 40-raqamli nusxa esa toʻq jigar rangli qattiq qogʻoz bilan muqovalangan.
                        <br>
                        <br>8725-raqamli nusxa ancha mukammal boʻlib, dastlabki varagʻida “Xorazm shahzodalarining
                        devonlari” deb yozilgan.
                        <br>
                        <br>Kitob toʻq yashil rangdagi qattiq qogʻoz bilan muqovalangan. Sahifalar raqamlanmagan. Har
                        bir varaqda 19-20 yozuv yoʻli bor. Ba`zi sahifalarning pastki qismi yirtilgan. Manba unchalik
                        yaxshi saqlanmagan. Ayrim varaqlar sargʻaygan. Bu toshbosma nusxa boʻlib, xati oʻzbekcha, yozuvi
                        O`rta Osiyo nasta`liq. She`rlar ikki ustun qilib berilgan. Gʻazallar tartibsiz raqamlangan.
                        Jumladan, manbaning Sultoniyga bagʻishlangan qismida ana shunday holatga duch kelamiz.
                        35-varaqdan Sultoniy ijodi berilib, 117 gʻazali keltiriladi. Gʻazallar 53-raqamdan boshlanib,
                        170-gʻazalda tugaydi. Soʻngra 207-gʻazal beriladi.
                        <br>
                        <br>Sultoniy ijodiga oid she`rlar orasida Oqil gʻazallari keltiriladi. Gʻazallar raqamlanmaydi.
                        Har biri “Gʻazali Oqil” deb beriladi. 83-84-varaqlarda Oqilning oltita gʻazali berilgach, yana
                        Sultoniy she`rlari keltiriladi. Shoir she`rlari 202–varaqda tugaydi. Uning 356 ta gʻazali
                        berilgan. Qolgan 99 gʻazal manbaga kiritilmagan.
                        <br>
                        <br>10281- raqamli toshbosma nusxa O`rta Osiyo nasta`liq xatida bitilgan. Hajmi 580 sahifa.
                        Majmua 42-sahifadan Bayoniy ijodi bilan boshlanadi. Dastlabki varaqlar yoʻqolgan. Sahifalar ikki
                        martadan raqamlangan boʻlib, raqamlar bir-biridan “ / ” belgisi bilan ajratilgan. Bayoniyning
                        350 ta gʻazali beriladi. Qolgan 8 ta gʻazal Sodiq ijodidan soʻng keltirilgan. 59–varaqda uch
                        bayt, 101–varaqda bir bayt, 107-varaqda esa 12 misra, 409-varaqda 13 misra she`r hoshiyaga
                        bitilgan. Sultoniyning 358-gʻazali tugallanmay qolgan, uning davomi Sa`diy gʻazallari orasida
                        berilgan. Sa`diy she`riyati manbaning soʻnggi qismida keltirilib, shoirga faqat sakkiz sahifa
                        ajratilgan, xolos.
                        <br>
                        <br>Toshbosma nusxalar orasida 8950-raqamli nusxa ham mavjud, birinchi titul varagʻida:
                        <br>
                        <br>Xiva noshiri: Muhammad Rahimxon Feruz.
                        <br>
                        <br>Haft shahzoda, –
                        <br>
                        <br>deb yozilgan. Ikkinchi titul varagʻida Muhammad Yusufbek Bayoniy – deb bitilgan. Manba oppoq
                        fabrika qogʻoziga qora siyohda bitilgan. Uni ancha mukammal nusxa sifatida qabul qilsak boʻladi.
                        Majmua toʻq yashil rangdagi qattiq qogʻoz bilan muqovalangan. Manba nasta`liq xatida bitilgan.
                        She`rlar ikki ustun shaklida berilgan boʻlib, har bir varaqda 19-20-21 yozuv yoʻli bor.
                        Sahifalar raqamlanmagan, she`rlar tartib raqami bilan belgilangan.
                        <br>
                        <br>Har bir shoir ijodiga bagʻishlangan qism yakunida “Soʻnggi aytgʻon gʻazallari” deb yana
                        gʻazallar keltiriladi va ular yangidan raqamlanadi. Manba yaxshi saqlangan.
                        <br>
                        <br>Majmuaning 8929-raqamli nusxasi qoʻlyozma boʻlib, 602 sahifani tashkil etadi. Manba qora
                        siyohda, nasta`liq xatida sariq fabrika qogʻoziga bitilgan. Kitob toʻq koʻk rangli qattiq qogʻoz
                        bilan muqovalangan. Qoʻlyozmani Muhammad Sharif devon ibn Ollohbergan devon koʻchirgan.
                        Majmuaning dastlabki sahifasiga arab alifbosi harflari yozilgan. 59-104-112–varaqlarning
                        hoshiyasiga pushti rang siyohda ayrim baytlar bitilgan. She`rlar ikki ustun qilib berilib, varaq
                        oxiriga keyingi sahifaning birinchi soʻzi yozilgan. Sahifalar raqamlangan, gʻazallar esa
                        hoshiyalar bilan ajratilgan. Nusxa unchalik yaxshi saqlanmagan boʻlib, ayrim sahifalarning
                        pastki qismidagi yozuvlarni umuman oʻqib boʻlmaydi. Har bir sahifada 20-21 yozuv yoʻli bor.
                        <br>
                        <br>“Haft shuaro” majmuasining yana bir qoʻlyozma nusxasi 14424 raqami bilan O`zR FA ShI
                        qoʻlyozmalar xazinasida saqlanmoqda. Qoʻlyozma 563 sahifadan iborat. Noma`lum kotib tomonidan
                        koʻchirilgan bu nusxa qora siyohda nasta`liq xatida koʻchirilgan. Sahifalar raqamlanmagan,
                        gʻazallar esa arab raqamlari bilan belgilangan. Gʻazallar ikki ustun shaklida berilgan. Kotib
                        Sultoniyning 18 gʻazalini Sa`diy gʻazallaridan soʻng keltiradi. Sa`diy qalamiga mansub qasida
                        tugallanmay qoladi. 67-sahifadan soʻng 112–sahifa keltiriladi. Bu kotibning manbani koʻchirish
                        jarayonida muayyan qoidaga amal qilmaganidan dalolatdir.
                        <br>
                        <br>Majmuaning yana ikkita toshbosma nusxasi mavjud boʻlib, ulardan biri f.f.n Sultonmirzo
                        Rahimovning shaxsiy kutubxonasida, ikkinchisi Xivaning Feruz muzeyida saqlanadi. Adabiyotshunos
                        Sultonmirzo Rahimov qoʻlidagi nusxa mukammal emas. Majmua 4-sahifadan boshlanadi. Manba qora
                        siyohda nasta`liq xatida bitilgan. Majmua Bayoniy she`rlari bilan boshlanadi. 44–sahifada shoir
                        she`rlari tugab, 207-231-sahifalarda davom etadi. 264-sahifada ham Bayoniy gʻazallari
                        keltiriladi.
                        <br>
                        <br>45-84-varaqlarda Oqil she`rlari beriladi. 85-155-sahifalarda Sultoniy qalamiga mansub
                        she`rlar keltiriladi. Shuningdek, 231-238-265-353–varaqlarda ham Sultoniy she`rlari berilgan.
                        <br>
                        <br>156-205–varaqlarga Mirzo ijodiga mansub she`rlar kiritilganki, bu holat boshqa nusxalarda
                        uchramaydi. Mirzo ijodi aslida “Haft shuaro” majmuasiga kiritilmagan boʻlib, bu holni
                        koʻchiruvchining muayyan maqsadi bilan yoxud tasodifiy fakt sifatida izohlash mumkin.
                        <br>
                        <br>Manbaning qolgan qismida Sodiq va Sa`diy she`rlari tartib bilan keltirilgan. Biroq Murodiy
                        va Farrux ijodiga ajratilgan qismlar yoʻqolgan. 59-71-72-101-107-409–varaqlarning hoshiyalariga
                        ayrim baytlar bitilgan.
                        <br>
                        <br>Majmuaning Xiva davlat muzeyida saqlanayotgan toshbosma nusxasi ham mukammal emas. Nusxa 257
                        varaqdan iborat boʻlib, 14-varaqdan 146-varaqqacha Sultoniy she`riyati beriladi. Unda shoirning
                        268 gʻazali jamlangan boʻlib, majmua oxirida yana 36 gʻazali keltiriladi.
                        <br>
                        <br>147-210-varaqlarda Sa`diy she`riyati beriladi. (162 gʻazali bor). Soʻngra Mirzo va Gʻulomiy
                        she`rlari keltiriladi. Bayoniyning 301 gʻazali berilib, qolgan qismlarda saroyda Feruz
                        gʻazallariga bitilgan payravlardan namunalar keltiriladi.
                        <br>
                        <br>Tadqiqotimizga majmuaning 10374 raqami bilan O`zR FA ShI qoʻlyozmalar xazinasida
                        saqlanayotgan toshbosma nusxani asos qilib oldik. Majmua och yashil rangdagi qattiq qogʻoz bilan
                        muqovalangan boʻlib, dastlabki varaq ochiq qolgan. Manba sargʻish Qoʻqon fabrika qogʻoziga qora

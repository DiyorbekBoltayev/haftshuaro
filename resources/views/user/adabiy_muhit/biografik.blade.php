                    <p class="mbr-text mbr-fonts-style display-7">Adabiyotshunos I.Haqqulov e`tirof etganidek, “ijodni
                        shoir shaxsiyatidan ajralgan bir holat deb boʻlmaydi, aksincha, uni shoir xarakteri, ruhiy
                        ehtiyojlari va ma`naviy imkoniyatlari bilan uzviy bogʻlash” muhimdir[1].
                        <br>
                        <br>Zero, yuqorida qayd etilgan tazkiralardagi “shoir shaxsiyatiga” oid ma`lumotlar “Haft
                        shuaro”ni tadqiq etishda qoʻshimcha materialdir.
                        <br>
                        <br>Majmuada yetti nafar shoir asarlaridan namunalar bilan birga, Sultoniy ijodi xususidagi
                        ma`lumotlar hamda Farruxni ulugʻlovchi boshlama joy olgan. Tazkiralarda esa ularga turlicha
                        yondashilgan. Jumladan, Laffasiy tazkirasida ular toʻliq saqlangan holda Tabibiy va Xodim
                        tazkiralarida Murodiy hamda Farruxga oid qismlar yoʻq. Shaxsan Feruz tavsiyasi va nazoratida
                        tuzilgan bu tazkiralarda shohning eng yaqin qarindoshlari – inisi hamda oʻgʻli asarlari va
                        ma`lumotlarining uchramasligi besabab emas. Nazarimizda, Murodiy va Farrux Tabibiy
                        tazkiralaridagi Feruz gʻazallariga payrav yozishmagan yoxud yozganlari yetarli boʻlmagan
                        koʻrinadi. Tabibiy qolgan besh nafar shoirning har biriga xos asosiy xususiyatlarni koʻrsatib,
                        shunday ta`riflaydi: “maoniy durining gʻilofi” Sultoniy, “ulum ichra monandi yoʻq” Sodiq,
                        “gʻazal ahli shirin bayoni” Sa`diy, “xushfahmu fozil, adibu komil” Bayoniy, “hamma nuktasi asru
                        shirin” Oqil va ularning payrav gʻazallari oldidan bitgan ma`lumot - masnaviylarida bu fikrlar
                        yanada kengroq ifodalanadi.
                        <br>
                        <br>“Haft shuaro”ning badiiy asarlar qismi Murodiyning (1854-1908) yigirma gʻazali, besh
                        muxammas, bir musaddas, bir qasida va bir ruboiysi bilan boshlanadi. Turli janrdagi bu asarlar
                        asl ismi Toʻramurod boʻlgan Murodiyning ancha yetuk shoir ekanini koʻrsatadi. Shoir haqidagi bu
                        fikrni Laffasiyning tazkirasida keltirgan quyidagi mulohazalar ham isbotlaydi: “Murodiyning
                        she`rlari ma`ni jihatdan bir daryoi ummon boʻlib, ilm gʻavvoslari durri javohir, la`li sadaf
                        ijrosidin hosil boʻlgani kabi, Murodiyning dil she`ridin yuzlarcha ma`ni mafgʻul hosil qilur
                        erdilar”[2].
                        <br>
                        <br>Muhammad Yusufbek Bayoniy bu fikrlarni yanada toʻldirib, “Toʻramurod toʻrakim, xon
                        hazratlarining birodarlari erdilar, ellik toʻrt yoshlarida olami foniydin saroyi boqiyga rihlat
                        koʻrguzdilar. Ul hazratdin ikki oʻgʻil yodgor qoldi. Biri Rahmonquli toʻra va biri Xudoyquli
                        toʻradirlar”, – deb yozadi va Murodiyga bagʻishlangan gʻazalini keltiradi[3].
                        <br>
                        <br>Majmuadagi shoirlardan biri Farrux (1873-1918) boʻlib, u Feruzning oʻgʻli Asfandiyordir. U
                        haqda majmuada shunday soʻzlar bitilgan:
                        <br>
                        <br>“Bismillahir rahmanir rahiym. Sayid Asfandiyor toʻra Zayd Umar mutaxallusi Farruxi valadi
                        Sulaymon nishon va Sayid Muhammad Rahim Bahodirxoni Soniy davom davlati va shavkati”[4].
                        <br>
                        <br>Farrux shaxsiyati va ijodi haqida kitobda boshqa ma`lumotlar yoʻq. Bu holni tushunish
                        mumkin. Nazarimizda, Bayoniy sanoqligina she`rlar yozgan valiahd “shoir”ni ortiqcha maqtashdan
                        koʻra, uning ijodi xususida sukut saqlashni ma`qul koʻrgan. Ba`zi ma`lumotlarni shu davrdan 38
                        yil oʻtgach tuzilgan Laffasiy tazkirasida uchratamiz. Unda Asfandiyor yoshligidan ancha
                        bilimlarni egallagani, ma`rifatga intilgani va “otasi Muhammad Rahimxonga ma`qul boʻlmoqlik
                        uchun Farrux taxallusi bilan she`r mashq qilib, oshiqona abyotlar yozgani” ta`kidlanib,
                        shaxsiyati xususida yana quyidagilar bitilgan: “Xiva xonligi hukumatiga ega boʻlgach, ul
                        (Asfandiyor – I.X) oʻziga kibr paydo qilib, zolimona ishlar qilib, fuqarolarga jabr-sitamlar
                        etib” – “hamisha fikridir ishqi majoziy, parichehra gulandomlar birla soziy”(B.30-31). Bu
                        oʻrinda Laffasiy Farrux xarakteri, xulq–atvoriga xolis yondashib, uning shoirligi otasiga ma`qul
                        boʻlish majburiyatining natijasi ekaniga alohida urgʻu beradiki, bu fikr Farruxning 1906 yildan
                        soʻng ijod qilmagani va avval muxtasar devon tuzganiga (jami 17 gʻazal, 2 muxammas, 1 ruboiy)
                        guvohlik beradi. Koʻpchilik she`rlarida ham tor intim kechinmalarning talqini ustuvorligini
                        “Haft shuaro”dagi she`rlari koʻrsatib turibdi. Nazarimizda Asfandiyorxonning shoh va shoir
                        sifatidagi shaxsiyati juda murakkab. Keyingi davrlarda xon sifatida qoralangan Asfandiyorni
                        shoir Farrux sifatida e`tirof etish koʻzga tashlanmoqda. Ushbu tamoyilni adabiyotshunos
                        H.Abdullayev quyidagicha rad etadi: “tarixda zolimligi bilan nom chiqargan, oʻnlab norasida
                        qizlarning hayotini xazon etgan xonning “she`riyat”ini nahotki uning siyosati, axloqi va
                        faoliyatidan ajratib boʻlsa? Zero, xon sifatida insonni tahqirlagan shaxsni ayni paytda shoir
                        sifatida insoniy muhabbatni ulugʻlashiga ishonib boʻladimi?”[5]
                        <br>
                        <br>Biroq 2006-yili chop etilgan “Farrux. Yod etib mandin degil”[6] nomli kitobda
                        Asfandiyorxonning shoh va shoir sifatidagi shaxsiyati yangi nuqtai nazardan ijobiy hamda xolis
                        yoritilgan. Adabiyotshunos olim N.Jumaxoʻjaning “Bunyodkor inson, noziktab` shoir” sarlavhali
                        soʻzboshisi hamda J.Joʻrayevning soʻngsoʻzida Asfandiyorxon – Farruxning shaxsiyati, ijodiyoti
                        atroflicha tahlil etilgan.
                        <br>
                        <br>Darhaqiqat, ijodkor asari uning shaxsi va dunyoqarashi mujassamining ifodasi ekanligi
                        haqidagi adolatli fikrni quvvatlaymiz.
                        <br>
                        <br>O`z asarlari bilan majmuadan eng salmoqli oʻrin egallagan shoir Sultoniydir (1889-1944).
                        Feruzning sevimli nabirasi boʻlgan Said Nosir toʻraga bu taxallusni bobosi bergan. Bayoniydan
                        ta`lim olgan Sultoniy turli janrlarda ijod qilgan. Majmuadagi Bayoniyning soʻzboshisida unga
                        shunday ta`rif beriladi: “Kim ushbu shahzodayi mushorun ilayh chun podshohi oliyjoh atoullohu
                        taolo umruhu va davlatuhu nabiralari jumlalaridin erdilar. Soyai inoyatlarida ta`lim va
                        tarbiyatlari birla sarafroz boʻlib, ham ul turluk fazlu hunarlar birlakim, oning birlan el
                        oralarida mashhurdirlar, shuhrai ofoq boʻldilar. Ash`orlari el oralarida mutafarriq boʻlib, har
                        kimga taajjub yuzidan oning tamoshosiga shavq paydo boʻlur va har koʻngulga oning ma`nolari
                        andishasida va inbisot yuzlanur erdi” (B.36).
                        <br>
                        <br>“Turli fazlu hunarlar birla mashhur”, “ash`orlari el oralarida mutafarriq” boʻlgan Sultoniy
                        haqida tazkiralarda ham yuqoridagi fikrga monand ancha batafsil ma`lumotlar bor. Jumladan,
                        Tabibiyda :
                        <br>
                        <br>Chu Sultoniy xushfazl shoir erur,
                        <br>
                        <br>Gʻazal degali asru mohir erur (B.1675).
                        <br>
                        <br>Laffasiyda: “Sultoniy Feruzga payrav ancha gʻazallar yozib turgʻonidan boshqa . . . bir
                        devon asar ham yozadi. Sultoniyning koʻpchilik gʻazallari oshiqona va falsafalardan iboratdur”
                        (B.32).
                        <br>
                        <br>Xodimda: “Shoir Sultoniy – Nosir toʻra mansabdor boʻlgan emas. Yusufbek (Bayoniy – I.X) unga
                        yetti yoshidan boshlab she`r mashqi ta`limini bergan. Toʻra bitgan she`rini oʻz qoʻli birla
                        Muhammad Rahimxonga oʻzi kiritib berar edi” (B.31-32).
                        <br>
                        <br>Turli davrlar mualliflariga mansub bu fikrlar Sultoniyning talantli shahzoda shoirlardan
                        biri va saroy adabiy muhitining faol vakili ekaniga dalildir.
                        <br>
                        <br>She`rlari bilan majmuadan oʻrin olgan shoirlardan yana biri Sodiqdir (1860-1930). Uning
                        shaxsiyati va ijodi xususida tazkiralardagi ma`lumotlar diqqatga molikdir. Ulardan ma`lum
                        boʻlishicha, shoirning ismi Rahmonquli boʻlib, u shoir Murodiyning oʻgʻli, ya`ni Feruzning
                        jiyanidir. Fors, arab tillarini, adabiyotini, Xorazm tarixini puxta bilgan Sodiq talantli
                        tarjimon va hattot ham boʻlgan. Tabibiy uni:
                        <br>
                        <br>“Dagʻi Sodiq ul sohibi ilmu hol,
                        <br>
                        <br>Mudom etgusi ma`ni darsin xayol” (B.1176),–
                        <br>
                        <br>deya ta`rif etsa, Laffasiy she`rlarini daryoi ummondagi durri javohirga qiyos kilar ekan,
                        yana bir iqtidori haqida yozadi: “Sodiq fasohat lisonligʻidin ziyodatiy xushxon boʻlganidek,
                        xatti suls, xatti kufiy, xatti rayhoniy, xatti shikastalarni ham ona tili kabi oʻqiganidek ham
                        yozar erdi” (B.34). Shuningdek, shoir Asfandiyor davrida haqorat va xusumatlar tufayli
                        xoʻrlangani ham qayd etiladi. Sodiqning tashqi va ma`naviy qiyofasi esa Xodim tazkirasida yorqin
                        aks etgan: “Rahmonquli madrasada oxun boʻlib, dars aytuvga ham kuchli edi. U qora soqol, uzun
                        chechak yuzli, xushfe`l, vijdonli odam edi. O`zi kambagʻal odam boʻlib, toʻralik dagʻdagʻasi
                        yoʻq edi. Yuvosh, lekin gʻoyat ishchan edi” (B.34-35).
                        <br>
                        <br>Manbalarda Sa`diy (vafoti 1937y) taxallusli shoir shahzoda ijodkorlar orasida oʻziga xos
                        jihatlari bilan ajralib turishi bayon etiladi. Ta`kidlanishicha, uning asl ismi Sa`dulla boʻlib,
                        u Feruzning oʻgʻli Said Abdulloning farzandi, shohning nevarasidir. Feruz tarbiyasida kamol
                        topgan, zullisonayn shoir boʻlgan, devon tuzgan, Bayoniydan ta`lim olgan. Tazkiralarda uning
                        shaxsiyati, ijtimoiy ahvoli va iqtidori xususida faqat ijobiy fikrlarni uchratamiz. Jumladan,
                        Tabibiy Sa`diy haqida:
                        <br>
                        <br>Durri bahri donish kalomi oning,
                        <br>
                        <br>Ki Sa`dulla shahzoda nomi oning (B.227), –
                        <br>
                        <br>deb yozadi. Laffasiyda esa boshqa shaxsiy sifatlari ham bayon qilingan. Chunonchi “...ul
                        yoshligʻidan jununta`b, koʻsa, qoʻtir, egni qisiq hoʻkki boʻlib, anchalik ilm-fan egasi
                        boʻladur, Feruzga payrav toʻrtlik abyotlar yozadur. Sa`diyning asarlari tamomi bir shoʻx
                        ma`shuqona boʻlsa-da, ma`ni jihatdan har bir dona bayti bir gavhari yakto erdi” (B.35-36).
                        <br>
                        <br>Bu fikrlar Xodim talqinida yanada kengayib, shoir xarakterining ayrim qirralari alohida qayd
                        etiladi. Tazkirachi yozadi: “Sa`dulla toʻra Xivaning tashqari qal`asida, Nurullabek hovlisida
                        tugʻilgan. U juda zehni oʻtkir odam edi. Bir koʻrgan narsasini, xoh arabiy boʻlsin, xoh forsiy
                        boʻlsin, darrov yodlab olar edi. Sa`dulla toʻra mansabdor boʻlgan emas. Har bir ishga mohir
                        boʻlgani uchun Yusufbek Bayoniy ustodligi birla oʻzi ham ilmga chanqoq odamligidan shoir
                        boʻlgʻon, 60 yoshlarida vafot etgan” (B.35-36).
                        <br>
                        <br>Shuningdek, Sa`diy zamondoshlari uni qiziqish doirasi keng shaxs, faol kitobxon va usta
                        shaxmatchi sifatida ham eslashadi.
                        <br>
                        <br>Tazkiralarda qayd etilishicha, shahzoda shoirlardan Oqil (1874-1929) oʻzining koʻp qirrali
                        faoliyati bilan oʻzgachalik kasb etgan ijodkordir. “Oqil” shoirning taxallusi boʻlib, asl ismi
                        Otabek boʻlib, Feruzning jiyani, ya`ni opasining oʻgʻlidir. Manbalarda Oqilni xarakterlovchi
                        umumiy yakdil fikr – uning yetuk shoir, san`atkor, musiqashunos, rassom, xattot va saxovatpesha
                        ekanligidir. Jumladan, Tabibiy Oqilning ijodiy iqtidorini yuksak baholab:
                        <br>
                        <br>Kelib Oqil ul hushmandu fahim,
                        <br>
                        <br>Tabiat ango borho mustaqim (B. 1010-1053), –
                        <br>
                        <br>desa, Laffasiy uning boshqa fazilatlarini kengroq aks ettiradi. Jumladan, u yozadi: “Oqil
                        ersa uzun boʻylugʻ, chechak yuzli, sariq mayiz, choʻqqi soqollik erdi, oning fazlini koʻrgan
                        zamono Arastu – Aflotunlari musallam qilur erdilar. Oqil doim jurnal, gazet oʻqib, dunyo
                        siyosatidin boxabar boʻlib turar erdi, oʻziga maxsus kitobxona – “Xayriyoti kutub” qilib, hamma
                        vaqtlar yetim oʻgʻlon, tul-bevalarni turlik ovqat va kiyimlar bilan shikasta dillarini marhamat
                        qilib turar erdi” (B.38). Bu sifatlar Xodim tazkiralarida saqlangani holda uning ijodiy
                        qobiliyati va xulq-atvori ta`rifiga alohida urgʻu beradi: “u eski maktabni toʻliq bitkazgan,
                        juda ham zehnli boʻlgan. Garchi madrasa ta`limini olmagan boʻlsa ham, olimlar bilan koʻp
                        suhbatlar qilgʻon. U turli ilmlar bilan tanish edi, 25 yoshidan shoirlik bilan shugʻullana
                        boshlagʻon, soz ilmidan olti yarim maqomni ham bilar edi, ham tanbur, ham gʻijjak, ham doira,
                        ham dutor cherta bilar, lekin ayta bilmas edi. Otabek toʻra juda ham saxiy edi, uzun boʻyli,
                        qora soqolli, xushbichim odam edi, soʻzlari mazmunli, shirali edi” (B.44).
                        <br>
                        <br>Manbalardagi eng batafsil adabiy ma`lumotlar Muhammad Yusufbek Bayoniyga (1840-1923)
                        tegishlidir. Garchi Bayoniy lirikasi adabiyotshunoslikda birmuncha oʻrganilgan, tarixiy asarlari
                        chop etilgan boʻlsa ham, u haqdagi tazkiralardagi ma`lumotlar, xususan, shaxsiyati bilan bogʻliq
                        tafsilotlar tahlil etilmay kelindi. Bu tabiiy. Chunki Bayoniy Feruzning eng yaqin doʻsti,
                        nevaralarining ustozi boʻlish bilan birga, adabiy muhitning yirik namoyandalaridan biri va
                        shohning yaqin maslakdoshi ham edi. Bu xususiyatlar uning barkamol inson va sermahsul ijodkor
                        sifatidagi shaxsiyatini el orasida mashhur qildi. Bu haqda tazkiralarda ham ancha ma`lumotlar
                        saqlanib qolgan. Tabibiy tazkirasida Bayoniy toʻgʻrisida quyidagi masnaviyni oʻqiymiz:
                        <br>
                        <br>Bayoniyki ul donish ogoh erur,
                        <br>
                        <br>Gʻazal ahliga murshidi roh erur (B.448).
                        <br>
                        <br>Bu oʻrinda Bayoniyning shoirlikda yuksak mahorati ta`kidlansa, boshqa manbalardagi talqinlar
                        bir-birini toʻldirib, shoirning mukammal qiyofasini gavdalantiradi. Laffasiy tazkirasining
                        “Bayoniykim Muhammad Yusufbek ahvoloti” qismida Bayoniyning forsiy, arab, turk tillarini
                        bilgani, tarixchi, tarjimon, ma`rifatparvarligi qayd etiladi va “yoshligʻidan yaxshigina
                        farosatligʻ, ziyrak boʻlib, akasi Yaxshimuroddan ta`lim oladi, tanbur, gʻijjakka ham ma`rifatli
                        boʻlib, shaxmatda ham juda tez fahmlik usto boʻladur”, – deyiladi (B.41).
                        <br>
                        <br>Xodim tazkirasida esa Bayoniy shaxsiyatining boshqa ibratli jihatlarga e`tibor berilgan. U
                        yozadi: “Eski maktabni bitirgach, tabiblik ilmidan ancha xabardor boʻlgʻon, madrasa ta`limini
                        olib, ilmi oshib ketgan. Yusufbek Bayoniy mansabdor boʻlgʻon emas, qoʻlining kuchi bilan kun
                        kechirgan, olti yarim maqom sozni toʻliq bilar edi. U koʻrinishdan uzun boʻyli, xushbichim,
                        sariq soqol, kambagʻal odam edi. Shunday boʻlsa ham, har bir kuni mehmonsiz oʻtmas edi,
                        kiyimlarini toza tutar, oʻzi oʻta ketgan saxiy, suhbati ma`noli, odatlari ham yaxshi, shirinsoʻz
                        kishi edi” (B.40-41).
                        <br>
                        <br>Diqqatga sazovor yana bir jihat shundaki, qayd etilgan tazkiralar va majmuada ikki mushtarak
                        fikr alohida ta`kidlanadi:
                        <br>
                        <br>1. Shahzoda shoirlar nasl-nasabidan qat`i nazar saroyda rasmiy mansab egallashmagan, ijodiy
                        faoliyatga ragʻbat bildirishgan;
                        <br>
                        <br>2. Adabiy muhit ijodkorlarining, jumladan, shahzoda shoirlarning taxalluslari shaxsan Feruz
                        tomonidan ma`qullangan yoxud taklif etilganki, bu ham shohning ijod ahliga befarq emasligiga
                        dalildir.
                        <br>
                        <br>Shu bilan birga, Feruzning ijodkorlar homiysi sifatidagi katta xizmatlarini baholashda
                        xolislik tamoyiliga amal qilib, uning hukmdor va oʻz davrining ilgʻor farzandi ekanligini
                        unutmasligimiz zarur. Zero, Feruzning adabiyot rivoji xususidagi gʻamxoʻrligida oʻz mavqeini
                        mustahkamlash maqsadi koʻzlanganini ham e`tirof etgan holda ayrim ijodkorlarga nisbatan jazo
                        me`yorini oshirib yuborganligini ham unutmaslik joiz. Chunonchi, Avazning ikki yuz darra bilan
                        kaltaklanib xoʻrlanishi, Masharif Tarro, Polli doʻzchi kabi san`atkorlarning qattiq jazolanishi,
                        Palmin va Xonim xalfalarning qopga solib, yuz darra urilishi kabi faktlarini ijobiy hodisa deb
                        boʻlmaydi. Nazarimizda, bu hodisalarda Feruzning hukmdorlik tabiati va xarakteridagi murakkablik
                        koʻproq namoyon boʻlganki, bularni ochiq aytish Feruz shaxsiga tabiiylik bagʻishlaydi, uning
                        taraqqiyparvar shoh sifatida shakllanish jarayoni real hayotiylikka ega ekanidan darak beradi.
                        Biroq Feruz shaxsidagi bu qusur uning ma`rifatparvarlik faoliyati mohiyatini oʻzgartira olmaydi.
                        <br>
                        <br>Yuqoridagi fikrlar bayon etilgan tazkiralarning “Haft shuaro”ga munosabatida yana bir
                        jihatni alohida ta`kidlash joiz: tazkiralar mualliflari majmuaga kirgan shahzoda shoirlar bilan
                        bir vaqtda va bir adabiy muhitda yashab ijod etishgan ma`rifatli, ziyoli shoirlar boʻlib,
                        oʻzlari bilgan adabiy-tarixiy ma`lumotlarga asoslanishgan. Shunga qaramay, oʻsha davr saroy
                        sharoiti va sub`ektiv sabablarga koʻra ularning qarashlarida tarafkashlik yoki faktlarni
                        sharhlashda noaniqliklarga yoʻl qoʻyish mavjud. Chunonchi, Tabibiy tazkirasi shahzoda
                        shoirlarning hammasi hayotligi davrida Feruz nazoratida tuzilgani bois ular sha`niga maqtov
                        ohanglarining ustuvor boʻlishi tabiiydir. Laffasiy tazkirasi esa XX asrning 40-yillarida
                        yaratilgani sababli unda tanqidiy munosabat aks etadi. Jumladan, Farruxning zolim va johilligi,
                        Sultoniy xarakteridagi xasislik, Xodim tazkirasida Sa`diy axloqidagi qusurlar qayd etiladi.
                        Shuningdek, tazkirachining “Bayoniy shoirlik bilan 40 yoshlarida shugʻullangan boʻlsa, ehtimol”,
                        degan fikri shubhalidir. Vaholanki, shoir bu yoshgacha qator she`rlar yozishga ulgurgan edi.
                        Ayrim shoirlarning tavallud va vafoti sanalari talqinida ham tafovutlar bor (Sodiq va Sa`diy).
                        Bu oʻrinda ta`kidlash kerakki, tazkiralardagi ma`lumotlarda tuzuvchilarning ayrim subyektiv
                        qarashlari, shaxsiy mayllari, turlicha yondashuvlari ham boʻlishi tabiiy. Buni Avaz haqidagi
                        ta`riflarda ham koʻrish mumkin. Jumladan, Laffasiy tazkirasida Avazning “xalq shikasta koʻngliga
                        yaqin fikrda ekanligi” ta`kidlansa, Xodim tazkirasida “ogʻzi juda beparovuz (shakkok) edi”, –
                        deyiladi. Shunga qaramay, har bir tazkira fakt va ma`lumotlar xarakteriga koʻra muhim va oʻziga
                        xosdir. Jumladan, Laffasiy tazkirasida shahzoda shoirlarning har biri ahvoloti bayoniga alohida
                        qism ajratilgan va ularning she`rlari asosida “Haft shuaro” tuzilgani takror qayd etilsa, Xodim
                        tazkirasida Feruz vafotidan soʻng shoirlarning jafoli hayoti, Sultoniy va Sa`diyning shoʻro
                        qatagʻoniga uchrashi va keyingi qismati xususida zarur ma`lumotlar berilgan.

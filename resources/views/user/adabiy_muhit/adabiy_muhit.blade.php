                        <div class="bottom-line">
                            <p class="mbr-text mbr-fonts-style display-7">Darhaqiqat, tarixiy taraqqiyot jarayonida
                                ajdodlarimiz tomonidan yaratilgan milliy-ma`naviy qadriyatlarni oʻrganish, ularni joriy
                                imloga oʻgirish va keng kitobxonlar ommasiga taqdim etish eng muhim masalalardan
                                biridir.
                                <br>
                                <br>Shunday qadriyatlarimizning bir qismi XX asr boshlarida yaratilgan boʻlib, unda
                                Xorazm adabiy muhiti alohida oʻrin tutadi. Bu muhitda asriy mumtoz an`analar davom
                                ettirilib va boyitilib, oʻziga xos adabiy maktab vujudga keldi. Shoh va shoir Feruz
                                (1844-1910) nomi bilan bogʻliq bu maktabda original va tarjima asarlar bilan birga,
                                kitobat hamda uning targʻiboti keng oʻrin tutib, bayoz, devon, tazkira va majmualar
                                tuzildi. Shulardan biri “Haft shuaro” majmuasi boʻlib, unda shahzoda shoirlarning
                                she`rlari toʻplangan.
                                <br>
                                <br>Shoʻro davri mafkurasi adabiy merosga partiyaviylik va sinfiylik tamoyillari asosida
                                yondashgani sababli, saroy muhiti bilan bogʻliq ijodkorlar va ularning asarlari,
                                jumladan “Haft shuaro” ham oʻrganilmay kelindi. Vaholanki, bu majmuada XIX asrning
                                oxiri, XX asrning boshida ijod qilgan va she`riyatda muayyan iz qoldirgan yetti nafar
                                shoirning turli janrlardagi merosidan namunalar jamlangan. Undagi she`riy asarlar
                                tarkibi, adabiy va biografik ma`lumotlar hozirga qadar ilmda yoritilmagan hamda oʻz
                                bahosini olmagan. Lekin bu meros oʻsha davr adabiy jarayonida faol qatnashgan yangi
                                ijodkorlarni kashf etishga, ular ijodini atroflicha tadqiq va targʻib qilishga hamda
                                adabiy muhit tarixi tadqiqining boʻsh qolgan sahifalarini toʻldirishga xizmat qiladi. Bu
                                esa Feruz davri adabiy muhiti, undagi badiiy ijodkorlik qirralari haqidagi mavjud
                                tasavvurlarimizni kengaytiradi. Shuningdek, “Haft shuaro” XX asr boshidagi adabiy
                                jarayonning oʻziga xos xususiyatlarini va Feruz shajarasiga mansub shoirlarning badiiy
                                ijoddagi hissasini aniqlash hamda davr adabiy muhitini yaxlit, uyushgan hodisa sifatida
                                tushunishga imkon beradi. Bu hol adabiyotshunoslikda xolislik tamoyili izchil amalga
                                oshirilayotgan hozirgi kunda, ayniqsa, muhim boʻlib, ushbu tadqiqotning dolzarbligini
                                belgilaydi.
                                <br>
                                <br>“Haft shuaro”ga munosabat dastlab shahzoda shoirlar zamondoshlarining asarlari va
                                esdaliklaridan boshlangan. Jumladan, Hasanmurod Laffasiy (1880-1949) tazkirasidagi
                                Murodiy, Farrux, Sultoniy, Sa`diy va Bayoniy haqidagi ma`lumotlarda ularning she`rlari
                                Feruzga ma`qul boʻlgach, xon ularni “Haft shuaro”ga kiritishni Bayoniyga topshirgani
                                qayd etiladi, lekin majmua haqida boshqa ma`lumotlar keltirilmaydi[1]. Bobojon
                                Tarroh-Xodim (1878-1971) oʻz esdaliklarining yaratilishi haqida shunday yozadi: “Shu
                                davrda boʻlgan butun shoirlarning nazmlarini Muhammad ikkinchiga oʻqib berar hamda nashr
                                qilishga yordam berar edim. O`ttiz bir nafar shoirning butun ahvoli menga ma`lum
                                boʻlgani uchun bu bayonotlarni oʻz qoʻlim, oʻz fikrim bilan yozib, sarhadiga
                                yetkurdim”[1]. Muhimi shundaki, ana shu oʻttiz bir nafar shoir orasida “Haft shuaro”ga
                                she`rlari kiritilgan Sultoniy, Sodiq, Sa`diy, Bayoniy va Oqil ham bor edi. Garchi Xodim
                                “Haft shuaro”ni bevosita tilga olmasa ham, yuqoridagi shoirlar tarjimai holi va
                                ijodining ayrim jihatlari xususida yangi ma`lumotni bayon etadi.
                                <br>
                                <br>“Haft shuaro” haqidagi fikrlar XX asrning 60-yillari boshidan qator maqola va
                                risolalarda koʻrina boshlaydi va tabiiyki, ularda bu davrdagi taomilga koʻra saroy
                                adabiy muhitiga, xususan, hukmdorlarga yaqin ijod ahli va ularning asarlariga salbiy
                                munosabat ustuvorlik qilardi. Shundan keyin oʻtgan salkam yarim asrlik davrda ushbu
                                majmuaga qiziqish davom etdi.
                                <br>
                                <br>Mazkur manbaning oʻrganilish davrini shartli ravishda ikki bosqichga ajratish
                                mumkin: 1) 60-yillardan 90-yillargacha; 2) mustaqillik davri bosqichi. Adabiy merosga
                                yondashuv xususiyatlari har bir bosqichda oʻziga xos ifodasini topdi. Birinchi bosqichda
                                “Haft shuaro”ga kirgan shoirlardan faqat Bayoniy ijodiga nisbatan xolislik koʻringan
                                boʻlsa, ikkinchi bosqichda yetti nafar shoir va ularning asarlari xususida ham muxtasar
                                boʻlsa-da, ayrim fikrlar aytila boshlandi.
                                <br>
                                <br>Akademik V.Abdullayevning 80-yillardagi tadqiqotlarida shahzoda shoirlarning
                                Navoiyga izdoshligi masalasiga doir fikrlar mavjud. Jumladan, adabiyotshunos “Navoiy va
                                bir davrning ikki shoiri” maqolasida Farrux va Bayoniyning Navoiy an`analariga
                                munosabatiga toʻxtalar ekan, Bayoniy dunyoqarashidagi nuqtai nazar va gʻoyaviy saviya
                                koʻlamini alohida ta`kidlab, uni “bedorlik, hushyorlik, ma`nidorlik shoiri”, Navoiyning
                                chinakam izdoshi sifatida ta`riflaydi[2]. “Navoiyga ijodiy izdoshlik mezoni haqida”
                                sarlavhali maqolasida esa Sultoniyning ayrim tatabbu` gʻazallari va taxmislarini tahlil
                                etib, unga “xalq hayoti, orzu-havasi bilan aloqador boʻlgan, Navoiydagi masalalarni
                                chetlab oʻtgan va Navoiydagi dunyoviylik mezonini buzib, ilohiylikni boʻrttirgan” shoir
                                sifatida ijodiga salbiy baho beradi[3].
                                <br>
                                <br>Adabiyotshunos N.Qobulov esa oʻzining “XIX asr oxiri va XX asr boshlarida Xorazmda
                                madaniy va adabiy hayot” maqolasida shahzoda shoirlar ijodiga munosabat bildirib,
                                “feodal saroy adabiyotining eng muhim belgilari taqlidchilik, shaklbozlik, payravchilik,
                                madhiyabozlik avj olishi bilan xarakterlanadi” deb hukmron mafkuraga muvofiq tavsiflaydi
                                va dalil sifatida Murodiy, Farrux, Sultoniy she`rlaridan ayrim parchalar keltiradi[4].
                                <br>
                                <br>90-yillarning boshidan mustaqillik mafkurasi tufayli yangilangan ilmiy tafakkurda
                                adabiy merosga xolis yondashuv namunalari Feruz shajarasi shoirlarining ijodini
                                oʻrganishda ham koʻrina boshladi. Bu sohada N.Jumaxoʻja, D.Rahim, Sh.Matrasul,
                                I.Adizova, G.Ismoilovalarning risola va maqolalari alohida e`tiborga loyiqdir. Xususan,
                                1991-1995 yillarda Feruzga bagʻishlab chop etilgan ommabop ikki kitobdagi tarixiy-badiiy
                                lavhalarda shahzoda shoirlarning hayoti va ijodi toʻgʻrisida qimmatli ma`lumotlar bayon
                                etilgan. Jumladan, birinchi kitobda Murodiy, Oqil, Sodiq, Sultoniy, Sa`diy tarjimai holi
                                va asarlariga doir muxtasar faktlar bor[5]. Ikkinchi kitobda yuqoridagi fikrlar davom
                                ettirilib va boyitilib, shahzoda shoirlar yetti nafarining ham badiiy merosi haqida fikr
                                yuritiladi va ularning adabiy muhitdagi oʻrni umumiy tarzda ta`kidlanib, quyidagi asosli
                                xulosaga kelinadi: “Haft shuaro” majmuasi ham Xorazm adabiy muhitini oʻrganishda muhim
                                manbalardan biri, uni oʻrganish va targʻib qilishdan adabiyot foyda koʻrsa koʻradiki,
                                aslo zarar koʻrmaydi. Shunday ekan, “Haft shuaro”, “Tazkirai si shuaroi Feruzshohiy”
                                kabi asarlarni nashr etish, ularni kitobxonlarga yetkazish bugunning talabi boʻlib
                                qolmoqda”[6].
                                <br>
                                <br>Adabiyotshunos G.Ismoilova bu fikrlarni quvvatlab, shunday yozadi: “Saroy maddohi”,
                                “taqlidchi”, “ijodi payrav she`rlardangina iborat” deb talqin qilingan koʻpgina qalam
                                ahlining she`rlarini sinchiklab kuzatsak, ularning zamona taqozosi bilan oʻz koʻnglidagi
                                nozik hislarni, qalbidagi alam-dardlarni nechogʻlik ustalik bilan ifoda etganlariga
                                guvoh boʻlamiz”[7].
                                <br>
                                <br>Shuningdek, saroy muhitidagi shoirlar ijodining adabiy-estetik qimmati va mumtoz
                                she`riyatimizni boyitishdagi ulushi xususida boshqa qator tadqiqotlarda ham ahamiyatga
                                molik fikrlar bayon etilgan[8].
                                <br>
                                <br>Koʻrinadiki, yuqorida qayd etilgan risola va maqolalarda “Haft shuaro”ni batafsil
                                oʻrganish, undagi shoirlar hayoti va she`rlarini monografik yoʻnalishda maxsus tadqiq
                                etish maqsadi qoʻyilmagan. Shu tufayli yangilangan davrda turli adabiy muhit va
                                yoʻnalishlardan iborat yagona oʻzbek milliy adabiyotini yaxlit holda birlamchi manbalar
                                asosida haqqoniy oʻrganish zaruriyati ham tadqiqot dolzarbligini oshiradi.
                                <br></p>
